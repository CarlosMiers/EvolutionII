package Modelo;

/**
 *
 * @author faisc
 */
public class cumplimiento_requisitos {
    int id;
    int tipo;
    String descripcion;
    
    public cumplimiento_requisitos(){
        
    }

    public cumplimiento_requisitos(int id, int tipo, String descripcion) {
        this.id = id;
        this.tipo = tipo;
        this.descripcion = descripcion;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    
    
}
