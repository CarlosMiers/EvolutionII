    private void GrabarItemSubActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarItemSubActionPerformed

        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar este Detalle? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {

            subcuentacliente dc = new subcuentacliente();
            subcuentaclienteDAO dcDAO = new subcuentaclienteDAO();

            if (this.nombre.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Ingrese Descripción");
                this.nombre.requestFocus();
                return;
            }
            Date FechaNacimiento = ODate.de_java_a_sql(fechanacimiento.getDate());

            dc.setNombre(nombre.getText());
            dc.setCedula(cedula.getText());
            dc.setRuc(ruc.getText());
            dc.setFechanacimiento(FechaNacimiento);
            dc.setIdcliente(nClienteBase);
            dc.setDireccion(direccion.getText());
            dc.setTelefono(telefono.getText());
            dc.setCelular(celular.getText());
            dc.setCorreo(correo.getText());
            dc.setIdcuenta(Double.valueOf(this.idcuenta.getText()));

            try {
                if (dc.getIdcuenta() == 0) {
                    dcDAO.InsertarSubCuenta(dc);
                } else {
                    dcDAO.actualizarSubCuenta(dc);
                    GrillaSubCuentas GrillaP = new GrillaSubCuentas();
                    Thread Hilo2 = new Thread(GrillaP);
                    Hilo2.start();
                    this.SalirSub.doClick();
                }
                // TODO add your handling code here:
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }

            GrillaSubCuentas GrillaP = new GrillaSubCuentas();
            Thread Hilo2 = new Thread(GrillaP);
            Hilo2.start();
            this.LimpiarSub();
            this.nombre.requestFocus();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_GrabarItemSubActionPerformed

