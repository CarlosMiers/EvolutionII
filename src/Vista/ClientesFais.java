    private class GenerarReporte extends Thread {

        public void run() {
            con = new Conexion();
            stm = con.conectar();
            try {
                Map parameters = new HashMap();
                //Ahora que hay parametros le enviamos uno con el mismo nombre del que creamos
                //en el reporte
                parameters.put("cNombreEmpresa", Config.cNombreEmpresa);
                parameters.put("nCliente", jTextOpciones1.getText().trim());
                JasperReport jr = null;
                URL url = getClass().getClassLoader().getResource("Reports/extracto_cuenta_clientes.jasper");
                jr = (JasperReport) JRLoader.loadObject(url);
                JasperPrint masterPrint = null;
                //Se le incluye el parametro con el nombre parameters porque asi lo definimos
                masterPrint = JasperFillManager.fillReport(jr, parameters, stm.getConnection());
                JasperViewer ventana = new JasperViewer(masterPrint, false);
                ventana.setTitle("Vista Previa");
                ventana.setVisible(true);
            } catch (Exception e) {
                JDialog.setDefaultLookAndFeelDecorated(true);
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", 1);
            }
        }
    }

    private class GrillaCliente extends Thread {

        public void run() {
            con = new Conexion();
            stm = con.conectar();
            int nAsesor = Integer.valueOf(Config.cCodigoAsesor);
            ResultSet results = null;
            if (nAsesor == 0) {
                System.out.println("SIN ASESOR " + nAsesor);
                if (filtroclientes.isSelected()) {
                    cSql = "select * from clientes where codfais>0 order by codfais";
                } else {
                    cSql = "select * from clientes order by codfais";
                }
            } else {
                if (filtroclientes.isSelected()) {
                    cSql = "select * from clientes where codfais>0 and asesor=" + nAsesor + " order by codfais";
                } else {
                    cSql = "select * from clientes where asesor=" + nAsesor + " order by codfais";
                }

                System.out.println("CON ASESOR" + nAsesor);
            }

            //Antes de Cargar el Jtable Ajustamos el ancho de las columnas con el Ancho que se nos antoje
            int cantidadRegistro = modelo.getRowCount();
            for (int i = 1; i <= cantidadRegistro; i++) {
                modelo.removeRow(0);
            }
            try {
                results = stm.executeQuery(cSql);
                while (results.next()) {
                    // Se crea un array que será una de las filas de la tabla.
                    // Se rellena cada posición del array con una de las columnas de la tabla en base de datos.
                    Object[] fila = new Object[15]; // Hay 14 columnas en la tabla
                    fila[0] = results.getString("codigo");
                    fila[1] = results.getString("codfais");
                    fila[2] = results.getString("nombre");
                    fila[3] = results.getString("comitente");
                    fila[4] = results.getString("ruc");
                    fila[5] = results.getString("direccion");
                    fila[6] = results.getString("telefono");
                    fila[7] = results.getString("fax");
                    fila[8] = results.getString("mail");
                    fila[9] = results.getString("profesion");
                    fila[10] = results.getString("lugartrabajo");
                    fila[11] = results.getString("cargolaboral");
                    fila[12] = formatoFecha.format(results.getDate("fechaingreso"));
                    fila[13] = results.getString("direccionlaboral");
                    fila[14] = results.getString("telefonolaboral");
                    modelo.addRow(fila);          // Se añade al modelo la fila completa.
                }
                jTable1.setRowSorter(new TableRowSorter(modelo));
                jTable1.updateUI();
                int cantFilas = jTable1.getRowCount();
                if (cantFilas > 0) {
                    jButton1.setEnabled(true);
                    jButton3.setEnabled(true);
                    jButton4.setEnabled(true);
                } else {
                    jButton1.setEnabled(false);
                    jButton3.setEnabled(false);
                    jButton4.setEnabled(true);
                }
                stm.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(new JFrame(),
                        "El Sistema No Puede Ingresar a los Datos",
                        "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                System.out.println(ex);
            }
        }
    }

