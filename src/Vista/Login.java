    private void BtnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIngresarActionPerformed
        String sCadena = null;
        try {
            BufferedReader bf = new BufferedReader(new FileReader("src/Txt/ip.txt"));
            while ((sCadena = bf.readLine()) != null) {
                Config.cIpServer = sCadena.toString();
            }
        } catch (FileNotFoundException ex) {
            Exceptions.printStackTrace(ex);
        } catch (IOException ex) {
            Exceptions.printStackTrace(ex);
        }

        SecureRandom random = new SecureRandom();
        byte bytes[] = new byte[20];
        random.nextBytes(bytes);
        String token = bytes.toString();

        Conexion con = null;
        Statement st = null;

        con = new Conexion();
        st = con.conectar();
        if (controlacceso > 3) {
            hand.mensaje_error("Intent칩 Ingresar al Sistema en tres ocasiones,"
                    + "Comuniquese con el Administrador");
            return;
        }
        if ((codusuario.getText().isEmpty()) || (pase.getText().isEmpty())) {
            hand.mensaje_error("Ingrese su Nombre de Usuario y Contrase침a");
        } else {
            try {
                ResultSet rs = null;
                String cSql = "SELECT * FROM usuarios WHERE password='"
                        + pase.getText() + "' AND last_name ='" + codusuario.getText() + "'";
                rs = st.executeQuery(cSql);
                rs.last();
                int encontrado = rs.getRow();
                System.out.println(encontrado);
                Config.cToken = token;
                Config.cCodigoAsesor = rs.getString("codasesor");
                Config.CodUsuario = rs.getString("employee_id");
                Config.cNombreUsuario = rs.getString("last_name");
                Config.cNivelUsuario = rs.getString("nivel");
                Config.cMenuParametros = rs.getString("parametros");
                Config.cMenuMercaderias = rs.getString("mercaderias");
                Config.cMenuCompras = rs.getString("compras");
                Config.cMenuVentas = rs.getString("ventas");
                Config.cMenuFinanzas = rs.getString("finanzas");
                Config.cMenuOperaciones = rs.getString("operaciones");
                Config.cMenuInmobiliaria = rs.getString("construcciones");
                Config.cMenuContabilidad = rs.getString("contabilidad");
                Config.cMenuImportaciones= rs.getString("importaciones");
                Config.cMenuHerramientas = rs.getString("herramientas");
                Config.cMenuAsociaciones = rs.getString("asociacion");
                Config.cMenuRRHH = rs.getString("rrhh");
                System.out.println(rs.getString("last_name"));
                if (encontrado == 1) {
                    this.setVisible(false);
//                   new PortadaCooperativa().setVisible(true);                    

//  this.accesoCasaBolsa(rs.getInt("codasesor"));
                                           new PortadaOpel().setVisible(true);
 //                                        new Portada().setVisible(true);
//                                              new PortadaFerremax().setVisible(true);
//                new PortadaRRHH().set1Visible(true);
 //                      new PortadaUAL().setVisible(true);
                } else {
                    hand.mensaje_error("Sus datos son incorrectos, Reviselos!");
                    controlacceso = controlacceso + 1;
                }
                rs.close();
                st.close();

            } catch (Exception e) {
                hand.mensaje_error("Sus datos son incorrectos, Reviselos!");
                controlacceso = controlacceso + 1;
                System.out.println(e);
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_BtnIngresarActionPerformed

