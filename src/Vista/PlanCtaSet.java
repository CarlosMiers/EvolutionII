    private void GrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed

        planDAO grabar = new planDAO();
        plan p = new plan();

        monedaDAO monDAO = new monedaDAO();
        moneda mon = null;
        try {
            mon = monDAO.buscarId(Integer.valueOf(this.moneda.getText()));
        } catch (SQLException ex) {
            Exceptions.printStackTrace(ex);
        }
        if (Integer.valueOf(codigo.getText()) > 0) {
            p.setCodigo(String.valueOf(codigo.getText()));
        }    //CAPTURAMOS LOS DATOS DE LA CABECERA

        p.setMoneda(mon);
        p.setCodigo(this.codigo.getText());
        p.setNombre(this.nombre.getText());
        p.setNaturaleza(this.naturaleza.getSelectedItem().toString());
        p.setNivel(Integer.valueOf(this.nivel.getValue().toString()));
        if (asentable.isSelected()) {
            p.setAsentable(1);
        } else {
            p.setAsentable(0);
        }

        ///////////////////////EMPEZAMOS A CAPTURAR DATOS NUMERICOS///////////////
        if (nModo.getText().equals("1")) {
            try {
                grabar.insertarPlan(p);
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
            }
        } else {
            try {
                grabar.actualizarPlan(p);
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
            }

        }

        detalle_plan.setVisible(false);
        this.detalle_plan.setModal(false);
        GrillaPlanCta GrillaPlan = new GrillaPlanCta();
        Thread HiloGrilla = new Thread(GrillaPlan);
        HiloGrilla.start();

    }//GEN-LAST:event_GrabarActionPerformed

