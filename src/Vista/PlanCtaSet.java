    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        nModo.setText("2");
        codigo.setEditable(false);
        nombre.requestFocus();
        this.limpiar();
        if (Integer.valueOf(Config.cNivelUsuario) < 3) {
        int nFila = this.jTable1.getSelectedRow();
        int nasentable = 0;
        if (nFila < 0) {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar un Registro");
            this.jTable1.requestFocus();
            return;
        } else {
            this.codigo.setText(this.jTable1.getValueAt(nFila, 0).toString());
        }
        planDAO planDAO = new planDAO();
        plan p = null;
        try {
            p = planDAO.buscarIdTodos(String.valueOf(this.codigo.getText()));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
        }
        if (p != null) {

            codigo.setEnabled(true);
            codigo.setText(idControl.getText());
            nombre.setText(p.getNombre());
            naturaleza.setSelectedItem(p.getNaturaleza());
            nivel.setValue(p.getNivel());

            // p.setNivel(Integer.valueOf(this.nivel.getValue().toString()));
            nasentable = p.getAsentable();
            if (nasentable == 1) {
                asentable.setSelected(true);
            } else {
                asentable.setSelected(false);
            }
            moneda.setText(String.valueOf(p.getMoneda().getCodigo()));
            nombremoneda.setText(p.getMoneda().getNombre());

            //   this.refrescarCarrusel(0);
            detalle_plan.setModal(true);

            //(Ancho,Alto)
            detalle_plan.setSize(785,380);
            //Establecemos un título para el jDialog
            detalle_plan.setTitle("Modificar Datos de la Unidad");
            detalle_plan.setLocationRelativeTo(null);
            detalle_plan.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "La Operación ya no puede Modificarse");
        }
        }else{
        JOptionPane.showMessageDialog(null, "Usuario no Autorizado");
         }

    }//GEN-LAST:event_jButton1ActionPerformed

