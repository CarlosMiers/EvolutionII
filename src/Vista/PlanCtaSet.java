    private class GrillaPlanCta extends Thread {

        public void run() {
            String formato = null;
            //Antes de Cargar el Jtable Ajustamos el ancho de las columnas con el Ancho que se nos antoje
            int cantidadRegistro = modelo.getRowCount();
            for (int i = 1; i <= cantidadRegistro; i++) {
                modelo.removeRow(0);
            }
            planDAO planDAO = new planDAO();
            try {
                for (plan pl : planDAO.todos()) {
                    // String.format("%1$#10s");

                    //   formato = "%1$#" + pl.getCodigo().length() + "s";
                    //       String nuevacadena=llenar(pl.getNombre(),pl.getNivel());
                    String nuevacadena = pl.getNombre();
                    String espacio = "";

                    switch (pl.getNivel()) {
                        case 2:
                            espacio = " ";
                            break;
                        case 3:
                            espacio = "  ";
                            break;
                        case 4:
                            espacio = "   ";
                            break;
                        case 5:
                            espacio = "    ";
                            break;
                        case 6:
                            espacio = "     ";
                            break;
                        case 7:
                            espacio = "      ";
                            break;
                    }

                    nuevacadena = espacio + pl.getNombre();
                    String cAsentable = "";
                    if (pl.getAsentable() == 0) {
                        cAsentable = "NO";
                    } else {
                        cAsentable = "SI";
                    }
                    String Datos[] = {String.valueOf(pl.getCodigo()), nuevacadena, pl.getNaturaleza(), String.valueOf(pl.getNivel()), cAsentable};
                    modelo.addRow(Datos);

                    // 
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
            }

            jTable1.setRowSorter(new TableRowSorter(modelo));
            int cantFilas = jTable1.getRowCount();
            if (cantFilas > 0) {
                jButton1.setEnabled(true);
                jButton3.setEnabled(true);
                jButton4.setEnabled(true);
            } else {
                jButton1.setEnabled(false);
                jButton3.setEnabled(false);
                jButton4.setEnabled(false);
            }
        }
    }

