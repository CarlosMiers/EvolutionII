        ///CADA CELDA 
        double suamortizacion = 0.00;
        double suinteres = 0.00;
        double suprestamo = 0.00;

        String camortiza = "";
        String cinteres = "";
        String ctotal = "";

        int totalRow = tablacupon.getRowCount();
