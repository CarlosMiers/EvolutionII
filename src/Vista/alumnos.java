    private void GrabarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarAlumnoActionPerformed
        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar esta Operación? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {
            Date FechaIngreso = ODate.de_java_a_sql(fechaingreso.getDate());
            Date FechaCulminacion = ODate.de_java_a_sql(fechaculminacion.getDate());

            clienteDAO actualDAO = new clienteDAO();
            cliente cli = new cliente();
            carreraDAO giDAO = new carreraDAO();
            carrera g = null;

            cli.setSemestre(semestre.getSelectedIndex() + 1);
            cli.setCodigo(Integer.valueOf(this.cuentasocio.getText()));
            cli.setFechaingreso(FechaIngreso);
            cli.setFechaculminacion(FechaCulminacion);
            cli.setNroregistro(nroregistro.getText());
            try {
                g = giDAO.buscarId(Integer.valueOf(this.carrera.getText()));
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
            if (g != null) {
                cli.setCarrera(g);
            } else {
                JOptionPane.showConfirmDialog(null, "Seleccione la Carrera");
                this.carrera.requestFocus();
                return;
            }

            try {
                actualDAO.ActualizarCarrera(cli);
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
            this.DatosSocios.setModal(false);
            this.DatosSocios.setVisible(false);
        }
    }//GEN-LAST:event_GrabarAlumnoActionPerformed

