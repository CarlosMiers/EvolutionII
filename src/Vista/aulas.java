    private void BotonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGrabarActionPerformed
        if (this.edificio.getText().isEmpty() || this.edificio.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Seleccione el Edificio");
            this.edificio.requestFocus();
            return;
        }
        if (this.nombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese del Nombre");
            this.nombre.requestFocus();
            return;
        }

        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar los Datos? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {

            aulaDAO grabar = new aulaDAO();
            aula aula = new aula();
            edificio_sedeDAO edifDAO = new edificio_sedeDAO();
            edificio_sede edif = null;

            try {
                edif = edifDAO.buscarId(Integer.valueOf(edificio.getText()));
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }

            int nestado = 0;
            if (this.estado.isSelected()) {
                nestado = 1;
            }
            aula.setNombre(nombre.getText());
            aula.setEdificio(edif);
            aula.setObservacion(observacion.getText());
            aula.setCapacidad(Integer.valueOf(capacidad.getText()));
            aula.setEstado(nestado);
            try {
                if (Integer.valueOf(codigo.getText()) == 0) {
                    grabar.insertarAula(aula);
                } else {
                    aula.setCodigo(Integer.valueOf(codigo.getText()));
                    grabar.actualizarAula(aula);
                }
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
            detalle_aulas.setVisible(false);
            detalle_aulas.setModal(false);

            GrillaAula GrillaOC = new GrillaAula();
            Thread HiloGrilla = new Thread(GrillaOC);
            HiloGrilla.start();
        }
    }//GEN-LAST:event_BotonGrabarActionPerformed

