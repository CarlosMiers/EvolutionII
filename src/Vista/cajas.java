    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (Integer.valueOf(Config.cNivelUsuario) < 3) {
            nestado = 0;
            int nFila = this.jTable1.getSelectedRow();
            this.codigo.setText(this.jTable1.getValueAt(nFila, 0).toString());
            cajaDAO cajaDAO = new cajaDAO();
            caja ca = null;
            try {
                ca = cajaDAO.buscarId(Integer.valueOf(codigo.getText()));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
            }
            if (ca != null) {
                codigo.setEnabled(false);
                nombre.setText(ca.getNombre());
                responsable.setText(ca.getResponsable());
                recibo.setText(formatosinpunto.format(ca.getRecibo()));
                factura.setText(formatosinpunto.format(ca.getFactura()));
                timbrado.setText(formatosinpunto.format(ca.getTimbrado()));
                iniciotimbrado.setDate(ca.getIniciotimbrado());
                vencetimbrado.setDate(ca.getVencetimbrado());
                expedicion.setText(ca.getExpedicion());
                nestado = ca.getEstado();
                if (nestado == 1) {
                    estado.setSelected(true);
                } else {
                    estado.setSelected(false);
                }
                //Establecemos un título para el jDialog

            }
            nombre.requestFocus();
            detalle_cajas.setModal(true);
            detalle_cajas.setSize(773, 470);
            //Establecemos un título para el jDialog
            detalle_cajas.setTitle("Actualización");
            detalle_cajas.setLocationRelativeTo(null);
            detalle_cajas.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Usuario no Autorizado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

