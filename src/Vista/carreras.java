    private void CargarMateria(int nsemestre) {
        int ncarrera = Integer.valueOf(this.codigo.getText());
        int cantidadRegistro = modelomateria.getRowCount();
        for (int i = 1; i <= cantidadRegistro; i++) {
            modelomateria.removeRow(0);
        }
        materiasDAO detDAO = new materiasDAO();
        try {
            for (materias detvta : detDAO.todosxCarreraxSemestre(ncarrera, nsemestre)) {
                String Detalle[] = {String.valueOf(detvta.getIdmateria()), detvta.getNombre(), String.valueOf(detvta.getCargahoraria()), String.valueOf(detvta.getCodigo())};
                modelomateria.addRow(Detalle);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
        }
        int cantFilas = tablamateria.getRowCount();
        if (cantFilas > 0) {
            BotonGrabarMateria.setEnabled(true);
            BotonBorrarMateria.setEnabled(true);
        } else {
            BotonGrabarMateria.setEnabled(true);
            BotonBorrarMateria.setEnabled(false);
        }

    }

    private void CargarSemestre() {
        int cantidadRegistro = modelosemestre.getRowCount();
        for (int i = 1; i <= cantidadRegistro; i++) {
            modelosemestre.removeRow(0);
        }
        semestresDAO detDAO = new semestresDAO();
        try {
            for (semestres detvta : detDAO.todosxCarrera(Integer.valueOf(this.codigo.getText()))) {
                String Detalle[] = {String.valueOf(detvta.getSemestre()), detvta.getNombre(), String.valueOf(detvta.getIditem())};
                modelosemestre.addRow(Detalle);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
        }
        int cantFilas = tablasemestre.getRowCount();
        if (cantFilas > 0) {
            BotonAgregarSemestre.setEnabled(true);
            BotonBorrar.setEnabled(true);
        } else {
            BotonAgregarSemestre.setEnabled(true);
            BotonBorrar.setEnabled(false);
        }

    }

