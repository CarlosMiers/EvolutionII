    private void BotonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarActionPerformed
        LimpiarSemestre();
        this.limpiar();
        if (Integer.valueOf(Config.cNivelUsuario) == 1) {
            int nFila = this.tablacarrera.getSelectedRow();
            if (nFila < 0) {
                JOptionPane.showMessageDialog(null, "Debe Seleccionar un Registro");
                this.tablacarrera.requestFocus();
                return;
            }

            this.codigo.setText(this.tablacarrera.getValueAt(nFila, 0).toString());
            carreraDAO sucDAO = new carreraDAO();
            carrera carrera = null;
            try {
                carrera = sucDAO.buscarId(Integer.valueOf(this.codigo.getText()));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
            }
            if (carrera != null) {
                nombre.setText(carrera.getNombre());
                facultad.setText(String.valueOf(carrera.getFacultad().getCodigo()));
                nombrefacultad.setText(carrera.getFacultad().getNombre());
                semestres.setText(String.valueOf(carrera.getSemestres()));
                titulo.setText(carrera.getTitulo());
                if (carrera.getEstado() == 0) {
                    this.estado.setSelected(false);
                } else {
                    this.estado.setSelected(true);
                }
                CargarSemestre();
                detalle_carreras.setModal(true);
                detalle_carreras.setSize(625, 490);
                //Establecemos un título para el jDialog
                detalle_carreras.setTitle("Modificar Datos de la Carrera");
                detalle_carreras.setLocationRelativeTo(null);
                detalle_carreras.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "USUARIO NO AUTORIZADO");
            }
        }
        //Envio como parametro el Codigo para saber si voy a modificar
        // TODO add your handling code here:
    }//GEN-LAST:event_BotonEditarActionPerformed

