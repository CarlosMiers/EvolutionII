    private void BotonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGrabarActionPerformed
        if (this.facultad.getText().isEmpty() || this.facultad.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Seleccione el Edificio");
            this.facultad.requestFocus();
            return;
        }
        if (this.nombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese del Nombre");
            this.nombre.requestFocus();
            return;
        }

        if (this.titulo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el Título a Obtener");
            this.titulo.requestFocus();
            return;
        }

        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar los Datos? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {

            carreraDAO grabar = new carreraDAO();
            carrera carrera = new carrera();
            facultadDAO facuDAO = new facultadDAO();
            facultad facu = null;

            try {
                facu = facuDAO.buscarId(Integer.valueOf(facultad.getText()));
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }

            int nestado = 0;
            if (this.estado.isSelected()) {
                nestado = 1;
            }
            carrera.setNombre(nombre.getText());
            carrera.setFacultad(facu);
            carrera.setTitulo(titulo.getText());
            carrera.setSemestres(Integer.valueOf(semestres.getText()));
            carrera.setEstado(nestado);
            try {
                if (Integer.valueOf(codigo.getText()) == 0) {
                    grabar.insertarCarrera(carrera);
                } else {
                    carrera.setCodigo(Integer.valueOf(codigo.getText()));
                    grabar.actualizarCarrera(carrera);
                }
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
            detalle_carreras.setVisible(false);
            detalle_carreras.setModal(false);

            GrillaCarrera GrillaOC = new GrillaCarrera();
            Thread HiloGrilla = new Thread(GrillaOC);
            HiloGrilla.start();
        }
    }//GEN-LAST:event_BotonGrabarActionPerformed

