        con = new Conexion();
        stm = con.conectar();
        ResultSet results = null;
        try {
            Connection conn = stm.getConnection();
            conn.setAutoCommit(false);
            try {
                stm.executeUpdate("DELETE FROM deducciones WHERE nprestamo=" + this.cNumeroControl.getText());
                int Items = jTable1.getRowCount();
                for (int i = 0; i < Items; i++) {
                    String IdFactura = (jTable1.getValueAt(i, 0).toString());
                    Date dEmision = ODate.de_java_a_sql(formatoFecha.parse(jTable1.getValueAt(i, 2).toString()));
                    String cConcepto = (jTable1.getValueAt(i, 16).toString());
                    String cNrocuota = (jTable1.getValueAt(i, 5).toString());
                    String cNroDocumento = (jTable1.getValueAt(i, 1).toString());
                    Date dVence = ODate.de_java_a_sql(formatoFecha.parse(jTable1.getValueAt(i, 3).toString()));
                    String cPago = (jTable1.getValueAt(i, 12).toString());
                    String cAmortiza = (jTable1.getValueAt(i, 6).toString());
                    String cInteres = (jTable1.getValueAt(i, 7).toString());
                    String cSaldo = (jTable1.getValueAt(i, 8).toString());
                    String cCapital = (jTable1.getValueAt(i, 13).toString());
                    String cMora = (jTable1.getValueAt(i, 9).toString());
                    String cGastos = (jTable1.getValueAt(i, 10).toString());
                    String cAcreedor = (jTable1.getValueAt(i, 14).toString());
                    String cCantidadCuotas = (jTable1.getValueAt(i, 15).toString());
                    String cPunitorio = (jTable1.getValueAt(i, 17).toString());

                    cPago = cPago.replace(".", "");
                    cPago = cPago.replace(",", ".");
                    cAmortiza = cAmortiza.replace(".", "");
                    cAmortiza = cAmortiza.replace(",", ".");
                    cInteres = cInteres.replace(".", "");
                    cInteres = cInteres.replace(",", ".");
                    cCapital = cCapital.replace(".", "");
                    cCapital = cCapital.replace(",", ".");
                    cSaldo = cSaldo.replace(".", "");
                    cSaldo = cSaldo.replace(",", ".");

                    if (cPunitorio.length() > 0) {
                        cPunitorio = cPunitorio.replace(".", "");
                        cPunitorio = cPunitorio.replace(",", ".");
                    } else {
                        cPunitorio = "0";
                    }

                    if (cMora.length() > 0) {
                        cMora = cMora.replace(".", "");
                        cMora = cMora.replace(",", ".");
                    } else {
                        cMora = "0";
                    }
                    if (cGastos.length() > 0) {
                        cGastos = cGastos.replace(".", "");
                        cGastos = cGastos.replace(",", ".");
                    } else {
                        cGastos = "0";
                    }
                    // SE GRABAN LOS DETALLES
                    if (cPago.length() > 1) {
                        String cSqlDet = "INSERT INTO deducciones(nprestamo,idfactura,documento,emision,concepto,cuota,numerocuota,vencimiento,supago,amortiza,minteres,saldo,capital,mora,gastos_cobranzas,punitorio,acreedor)";
                        cSqlDet += "VALUES ('" + this.cNumeroControl.getText() + "','" + IdFactura + "','" + cNroDocumento + "','";
                        cSqlDet += dEmision + "','" + cConcepto + "','" + cNrocuota + "','" + cCantidadCuotas + "','" + dVence + "','";
                        cSqlDet += cPago + "','" + cAmortiza + "','" + cInteres + "','" + cSaldo + "','" + cCapital + "','";
                        cSqlDet += cMora + "','" + cGastos + "','" + cPunitorio + "','" + cAcreedor + "')";
                        stm.executeUpdate(cSqlDet);
                    }
                }
                //PRIMERO EL GRABAMOS EL DETALLE DE CHEQUES EN 
                //LA TABLA detalle_descuento_documentos
                conn.commit();
                stm.close();
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            } catch (ParseException ex) {
                Exceptions.printStackTrace(ex);
                conn.rollback();
                stm.close();
            }
        } catch (SQLException ex) {
            Exceptions.printStackTrace(ex);
        }
        Descuentos.totaldescuentos = this.totalpago.getText();
        this.dispose();
