    private class ResumenCuenta extends Thread {

        String cSqlResumen = null;

        public void run() {
            con = new Conexion();
            stm = con.conectar();
            ResultSet results = null;
            cSqlResumen = "SELECT SUM(cuenta_clientes.amortiza) AS capital,";
            cSqlResumen += "SUM(cuenta_clientes.minteres) AS interes_ordinario,";
            cSqlResumen += "SUM(cuenta_clientes.saldo) AS totaldeuda,";
            cSqlResumen += "SUM(  IF( DATEDIFF(CURDATE(),vencimiento) <0 ,  cuenta_clientes.minteres, 0 ) ) AS interesnovencido,";
            cSqlResumen += "SUM(  IF( DATEDIFF(CURDATE(),vencimiento) >0,  cuenta_clientes.minteres, 0 ) ) AS interesvencido ";
            cSqlResumen += "FROM cuenta_clientes ";
            cSqlResumen += " WHERE cliente='" + codcliente.getText() + "' ";
            cSqlResumen += " AND saldo>0 ";
            cSqlResumen += " AND moneda='" + codMoneda.getText() + "' ";
            cSqlResumen += " AND comprobante IN(8,9,10)";
            try {
                results = stm.executeQuery(cSqlResumen);
                results.last();
                System.out.println(results.getString("capital"));
                capital.setText(formato.format(results.getDouble("capital")));
                totaldeuda.setText(formato.format(results.getDouble("totaldeuda")));
                interesordinario.setText(formato.format(results.getDouble("interes_ordinario")));
                interesvencido.setText(formato.format(results.getDouble("interesvencido")));
                interesavencer.setText(formato.format(results.getDouble("interesnovencido")));
                stm.close();
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
                System.out.println(ex);
            }

        }
    }

