    private void cargarTabla(String sql) {
        double nCapital = 0.00;
        double nMora = 0.00;
        double nInteres = 0.00;
        int nDiaGraciaMora = 0;
        double nImporteGastos, nDiasGracia, nDiasGraciaGastos = 0.00;
        double nPunitorio = 0.00;
        double nTasaPunitoria = 0.00;

        int natraso = 0;
        String catraso = null;

        DefaultTableCellRenderer TablaRenderer = new DefaultTableCellRenderer();
        TablaRenderer.setHorizontalAlignment(SwingConstants.RIGHT); // aqui defines donde alinear 

        con = new Conexion();
        stm = con.conectar();
        ResultSet results = null;
        int[] anchos = {5, 80, 80, 80, 60, 60, 80, 80, 80, 80, 80, 80, 80, 80, 80, 50, 50, 80};
        for (int i = 0; i < modelo.getColumnCount(); i++) {
            this.jTable1.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }

        this.jTable1.getColumnModel().getColumn(1).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(5).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(6).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(7).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(8).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(9).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(10).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(11).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(12).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(13).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(14).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(15).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(16).setCellRenderer(TablaRenderer);
        this.jTable1.getColumnModel().getColumn(17).setCellRenderer(TablaRenderer);

        int cantidadRegistro = modelo.getRowCount();
        for (int i = 1; i <= cantidadRegistro; i++) {
            modelo.removeRow(0);
        }
        try {
            results = stm.executeQuery(sql);
            while (results.next()) {
                // Se crea un array que será una de las filas de la tabla.
                Object[] fila = new Object[18]; // Hay 8 columnas en la tabla
                // Se rellena cada posición del array con una de las columnas de la tabla en base de datos.
                //Verificamos si tiene fecha de pago cargado
                //Si tiene fecha de pago
                //usamos como fecha de ultimo pago
                //de lo contrario la fecha de vencimiento
                catraso = null;
                nMora = 0.00;
                nImporteGastos = 0.00;
                nPunitorio = 0.00;
                nCapital = results.getDouble("saldo");
                catraso = results.getString("di");
                int ndiamora = results.getInt("atraso");
                natraso = results.getInt("di");
                nDiasGraciaGastos = results.getInt("diasgracia_gastos");
                nTasaPunitoria = results.getDouble("interespunitorio");
            
                if (ndiamora > 0 && catraso != null) {
                    if (natraso <= ndiamora) {
                        natraso = results.getInt("di");
                    } else {
                        natraso = ndiamora;
                    }
                } else {
                    natraso = ndiamora;
                }
                
                if (natraso > nDiaGraciaMora) {
                    nInteres = results.getDouble("interesmora");
                    nMora = Math.round(nCapital * ((nInteres / 100) / 360 * natraso));
                    nMora = Math.round(nMora + (nMora * Config.porcentajeiva / 100));
                    fila[9] = formato.format(nMora);
                } else {
                    fila[9] = formato.format(0);
                }

                if (nTasaPunitoria > 0 && nMora > 0) {
                    nPunitorio = Math.round(nMora * nTasaPunitoria / 100);
                }

                if (natraso > nDiasGraciaGastos) {
                    nImporteGastos = results.getDouble("gastoscobros");
                    fila[10] = formato.format(nImporteGastos);
                } else {
                    fila[10] = formato.format(0);
                }
                fila[17] = formato.format(nPunitorio);

                fila[0] = results.getString("iddocumento");
                fila[1] = results.getString("documento");
                fila[2] = formatoFecha.format(results.getDate("fecha"));
                fila[3] = formatoFecha.format(results.getDate("vencimiento"));
                fila[4] = results.getString("nomalias");
                fila[5] = formato.format(results.getDouble("cuota"));
                fila[6] = formato.format(results.getDouble("amortiza"));
                fila[7] = formato.format(results.getDouble("minteres"));
                fila[8] = formato.format(results.getDouble("saldo"));
                fila[11] = formato.format(nCapital + nMora + nImporteGastos + nPunitorio);
                fila[12] = formato.format(0);
                fila[13] = formato.format(results.getDouble("capital"));
                fila[14] = results.getString("inversionista");
                fila[15] = results.getString("numerocuota");
                fila[16] = results.getString("comprobante");
                modelo.addRow(fila);
            }

            this.jTable1.setRowSorter(new TableRowSorter(modelo));
            //  this.jTable1.updateUI();
            stm.close();
        } catch (SQLException ex2) {
            JOptionPane.showMessageDialog(new JFrame(),
                    "El Sistema No Puede Ingresar a la Consulta de Cuotas",
                    "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex2);
        } catch (Exception ex2) {
            JOptionPane.showMessageDialog(new JFrame(),
                    "El Sistema No Puede Conectarse a la Base de Datos",
                    "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex2);
        }
    }

