    private void GrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed
        if (this.nombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el Nombre o Denominación de la Cuenta");
            this.nombre.requestFocus();
            return;
        }
        BDConexion BD = new BDConexion();

        // Formateamos el campo de salario para enviarlo a la base de datos
        //    String ccategoria = (String) categoria.getSelectedItem();
        String ctasa = this.tasamora.getText();
        // con el metodo replace borramos los puntos y remplazamos la coma decimal
        // por el punto para su correcta captura en la base de datos
        if (this.tasamora.getText().trim().length() > 0) {
            ctasa = ctasa.replace(".", "").replace(",", ".");
        } else {
            ctasa = "0";
        }
        String cgastos = this.gastoscobros.getText();
        if (this.gastoscobros.getText().trim().length() > 0) {
            cgastos = cgastos.replace(".", "").replace(",", ".");
        } else {
            cgastos = "0";
        }

        String cPunitorio = this.interespunitorio.getText();
        if (this.interespunitorio.getText().trim().length() > 0) {
            cPunitorio = cPunitorio.replace(".", "").replace(",", ".");
        } else {
            cPunitorio = "0";
        }

        String cTasa = this.tasa.getText();
        if (this.tasa.getText().trim().length() > 0) {
            cTasa = cTasa.replace(".", "").replace(",", ".");
        } else {
            cTasa = "0";
        }

        String cTasaInteresMora = this.tasainteresmora.getText();
        if (this.tasainteresmora.getText().trim().length() > 0) {
            cTasaInteresMora = cTasaInteresMora.replace(".", "").replace(",", ".");
        } else {
            cTasaInteresMora = "0";
        }

        int nCategoria = this.categoria.getSelectedIndex() + 1;
        // Formateamos el campo de salario del conyugye para enviarlo a la base de datos
        // con el metodo replace borramos los puntos y remplazamos la coma decimal
        // por el punto para su correcta captura en la base de datos
        int nLibro=0;
        if(libros.isSelected()) {
            nLibro=1;
        }else{
            nLibro=0;
        }
        
        int nCodOld=0;
        if(codold.isSelected()) {
            nCodOld=1;
        }else{
            nCodOld=0;
        }
     
        
        if (this.codigo.getText().equals("0")) {
            BD.insertarRegistro("comprobantes", "nombre,tipo,interesmora,diasgracia,gastoscobros,diasgracia_gastos,nomalias,interespunitorio,tasainteres,tasainteresmora,libros,codold", "'" + nombre.getText() + "','" + nCategoria + "','" + ctasa + "','" + diagraciamora.getText() + "','" + cgastos + "','" + this.diasgraciagastos.getText() + "','" + nomalias.getText() + "','" + cPunitorio + "','" + cTasa + "','" + cTasaInteresMora+"','"+nLibro+"','"+nCodOld + "'");
        } else {
            BD.actualizarRegistro("comprobantes", " nombre='" + nombre.getText() + "',tipo='" + nCategoria + "',interesmora='" + ctasa + "',diasgracia='" + diagraciamora.getText() + "',gastoscobros='" + cgastos + "',diasgracia_gastos='" + this.diasgraciagastos.getText() + "',nomalias='" + this.nomalias.getText() + "',interespunitorio='" + cPunitorio + "',tasainteres='" + cTasa + "',tasainteresmora='" + cTasaInteresMora+"',libros='"+nLibro+"',codold='"+nCodOld + "'", "codigo= " + this.codigo.getText());
        }

        this.detalle_comprobantes.setModal(false);
        this.detalle_comprobantes.setVisible(false);
        GrillaComprobante GrillaComprobante = new GrillaComprobante();
        Thread HiloGrilla = new Thread(GrillaComprobante);
        HiloGrilla.start();
        // TODO add your handling code here:
    }//GEN-LAST:event_GrabarActionPerformed

