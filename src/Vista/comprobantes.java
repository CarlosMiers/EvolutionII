    private void AgregarTasaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarTasaActionPerformed
        String cCuota2 = this.cuota2.getText();
        cCuota2 = cCuota2.replace(".", "").replace(",", ".");

        String cCuota3 = this.cuota3.getText();
        cCuota3 = cCuota3.replace(".", "").replace(",", ".");

        String cGastos = this.importegastos.getText();
        cGastos = cGastos.replace(".", "").replace(",", ".");

        if (Double.valueOf(cCuota2) <= 0) {
            JOptionPane.showMessageDialog(null, "Ingrese Importe Desde");
            this.cuota2.requestFocus();
            return;
        }
        if (Double.valueOf(cCuota3) <= 0) {
            JOptionPane.showMessageDialog(null, "Ingrese Importe Hasta");
            this.cuota3.requestFocus();
            return;
        }
        if (Double.valueOf(cGastos) <= 0) {
            JOptionPane.showMessageDialog(null, "Ingrese el Gasto Administrativo");
            this.importegastos.requestFocus();
            return;
        }

        cuoterocreditoDAO grabarL = new cuoterocreditoDAO();
        cuoterocredito L = new cuoterocredito();
        //Clase de Cliente porque tiene que hacer referencia al cliente
        comprobanteDAO comDAO = new comprobanteDAO();
        comprobante cm = null;
        try {
            cm = comDAO.buscarId(Integer.valueOf(this.idControl.getText()));

        } catch (SQLException ex) {
            Exceptions.printStackTrace(ex);
        }

        L.setComprobante(cm);
        L.setCuota2(Double.valueOf(cCuota2));
        L.setCuota3(Double.valueOf(cCuota3));
        L.setImporte(Double.valueOf(cGastos));
        try {
            grabarL.insertarCuotero(L);
            // TODO add your handling code here:
        } catch (SQLException ex) {
            Exceptions.printStackTrace(ex);
        }
        GrillaTasas GrillaL = new GrillaTasas();
        Thread HiloTa = new Thread(GrillaL);
        HiloTa.start();
        this.cuota2.setText("0");
        this.cuota3.setText("0");
        this.importegastos.setText("0");

        // TODO add your handling code here:
    }//GEN-LAST:event_AgregarTasaActionPerformed

