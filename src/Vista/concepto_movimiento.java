    public void cargarTabla(String sql) {
        //Uso la Clase SimpleDateFormat para darle formato al campo fecha
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
        //Llamo a la clase conexion para conectarme a la base de datos
        con = new Conexion();
        stm = con.conectar();
        ResultSet results = null;
        //Antes de Cargar el Jtable Ajustamos el ancho de las columnas con el Ancho que se nos antoje
        int[] anchos = {70, 300, 100, 100, 100, 350, 350, 200};
        for (int i = 0; i < modelo.getColumnCount(); i++) {
            this.jTable1.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }

        int cantidadRegistro = modelo.getRowCount();
        for (int i = 1; i <= cantidadRegistro; i++) {
            modelo.removeRow(0);
        }
        try {
            results = stm.executeQuery(sql);
            while (results.next()) {
                // Se crea un array que ser谩 una de las filas de la tabla.
                // Se rellena cada posici贸n del array con una de las columnas de la tabla en base de datos.
                Object[] fila = new Object[8]; // Hay 10 columnas en la tabla
                fila[0] = results.getString("codigo");
                fila[1] = results.getString("Nombre");
                fila[2] = results.getString("Tipo");
                fila[3] = results.getString("Estado");
                fila[4] = results.getString("Id.Cta");
                fila[5] = results.getString("Descripci贸n Contable");

                modelo.addRow(fila);          // Se a帽ade al modelo la fila completa.
            }
            this.jTable1.setRowSorter(new TableRowSorter(modelo));
            this.jTable1.updateUI();
            int cantFilas = this.jTable1.getRowCount();
            if (cantFilas > 0) {
                this.Modificar.setEnabled(true);
                this.Eliminar.setEnabled(true);
                this.Listar.setEnabled(true);
            } else {
                this.Modificar.setEnabled(false);
                this.Eliminar.setEnabled(false);
                this.Listar.setEnabled(true);
            }
            stm.close();
        } catch (SQLException ex2) {
            JOptionPane.showMessageDialog(new JFrame(),
                    "El Sistema No Puede Ingresar a los Datos",
                    "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex2);
        }
    }

