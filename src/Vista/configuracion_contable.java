    private void GrabarConfigActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarConfigActionPerformed
        Object[] opciones = {"  Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Confirma la Actualización ? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {
            config_contableDAO GrabarCo = new config_contableDAO();
            config_contable cf = new config_contable();
            cf.setRepresentante_legal(this.representante_legal.getText());
            cf.setContador(this.contador.getText());
            cf.setRuc_contador(this.ruc_contador.getText());
            cf.setVentacontado(ventacontado.getText());
            cf.setVentacredito(ventacredito.getText());
            cf.setIvacompra10(ivacompra10.getText());
            cf.setIvacompra5(ivacompra5.getText());
            cf.setIvaventa10(ivaventa10.getText());
            cf.setIvaventa5(ivaventa5.getText());
            cf.setResultado_anterior(resultado_anterior.getText());
            cf.setResultado_actual(resultado_actual.getText());
            cf.setPeriodocontable(Integer.valueOf(periodocontable.getText()));
            cf.setRetencioncompra(retencioncompra.getText());
            cf.setRetencionventa(retencionventa.getText());
            cf.setGastos((gastos.isSelected()) ? 1 : 0);
            cf.setCompras((compras.isSelected()) ? 1 : 0);
            cf.setPagos((pagos.isSelected()) ? 1 : 0);
            cf.setCobranzas((cobranzas.isSelected()) ? 1 : 0);
            cf.setDepositos((depositos.isSelected()) ? 1 : 0);
            cf.setExtracciones((extracciones.isSelected()) ? 1 : 0);
            cf.setFondofijo((fondofijo.isSelected()) ? 1 : 0);
            cf.setVentas((ventas.isSelected()) ? 1 : 0);
            cf.setVentaexenta(ventaexenta.getText());
            try {
                GrabarCo.ConfigContable(cf);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
            }
            JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            this.SalirConfig.doClick();
        } else {
            JOptionPane.showMessageDialog(null, "Algunos Datos son Necesarios");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_GrabarConfigActionPerformed

