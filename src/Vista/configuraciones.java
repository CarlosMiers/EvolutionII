    private void sucursalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sucursalActionPerformed
        sucursalDAO bussuc = new sucursalDAO();
        sucursal suc = null;
        try {
            suc = bussuc.buscarId(Integer.valueOf(this.sucursal.getText()));
            if (suc.getCodigo() == 0) {
                JOptionPane.showMessageDialog(null, "Sucursal no Existe");
            } else {
                nombresucursal.setText(suc.getNombre());
                //Establecemos un título para el jDialog
                this.vendedor.requestFocus();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_sucursalActionPerformed

