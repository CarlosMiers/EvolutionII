    private void GrabarConfigActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarConfigActionPerformed
        Object[] opciones = {"  Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Confirma la Actualización ? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {
            configuracionDAO GrabarCo = new configuracionDAO();
            configuracion cf = new configuracion();
            cf.setEmpresa(this.nombreempresa.getText());
            cf.setRuc(this.ruc.getText());
            cf.setDireccion(this.direccion.getText());
            cf.setTelefono(this.telefono.getText());
            cf.setFax(this.fax.getText());
            cf.setRamo(this.ramo.getText());
            cf.setResponsable(this.responsable.getText());
            cf.setPeriodoactivo(Integer.valueOf(this.periodoactivo.getValue().toString()));
            cf.setMail(this.mail.getText());
            cf.setWeb(this.web.getText());

            if (this.nombreempresa.getText().compareTo("") != 0 && ruc.getText().compareTo("") != 0) {
                try {
                    GrabarCo.ConfigInicial(cf);
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Algunos Datos son Necesarios");
            }

        }
        // TODO add your handling code here:
    }//GEN-LAST:event_GrabarConfigActionPerformed

