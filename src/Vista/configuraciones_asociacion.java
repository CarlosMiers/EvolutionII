    private void GrabarConfigActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarConfigActionPerformed
        Object[] opciones = {"  Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Confirma la Actualización ? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {
            configuracionDAO GrabarCo = new configuracionDAO();
            configuracion cf = new configuracion();
            cf.setEmpresa(this.nombreempresa.getText());
            cf.setRuc(this.ruc.getText());
            cf.setDireccion(this.direccion.getText());
            cf.setTelefono(this.telefono.getText());
            cf.setNombrepagare(this.codigoaporte.getText());
            cf.setNombresolicitud(this.codigocuotasocial.getText());
            cf.setNombreliquidacion(this.codigosolidaridad.getText());
            cf.setNombrecontrato1(this.codigointeres.getText());
            cf.setNombrefactura(this.codigoordencompra.getText());
            cf.setNombrerecibo(this.codigoordencredito.getText());

            String cCapitalizacion = this.capitalizacion.getText();
            cCapitalizacion = cCapitalizacion.replace(".", "").replace(",", ".");
            cf.setCapitalizacionaporte(Double.valueOf(cCapitalizacion));

            String cArancelGastos = this.arancelgastos.getText();
            cArancelGastos = cArancelGastos.replace(".", "").replace(",", ".");
            cf.setArancelgastos(new BigDecimal(Double.valueOf(cArancelGastos)));

            String cArancelSeguro = this.arancelseguro.getText();
            cArancelSeguro = cArancelSeguro.replace(".", "").replace(",", ".");
            cf.setArancelseguro(new BigDecimal(Double.valueOf(cArancelSeguro)));

            String cArancelAporte = this.arancelaporte.getText();
            cArancelAporte = cArancelAporte.replace(".", "").replace(",", ".");
            cf.setArancelaporte(new BigDecimal(Double.valueOf(cArancelAporte)));

            String cArancelSolidaridad = this.arancelsolidaridad.getText();
            cArancelSolidaridad = cArancelSolidaridad.replace(".", "").replace(",", ".");
            cf.setArancelsolidaridad(new BigDecimal(Double.valueOf(cArancelSolidaridad)));

            String cArancelCobrador = this.arancelcobrador.getText();
            cArancelCobrador = cArancelCobrador.replace(".", "").replace(",", ".");
            cf.setArancelcobrador(new BigDecimal(Double.valueOf(cArancelCobrador)));

            String cArancelFondo = this.arancelfondoproteccion.getText();
            cArancelFondo = cArancelFondo.replace(".", "").replace(",", ".");
            cf.setArancelfondoproteccion(new BigDecimal(Double.valueOf(cArancelFondo)));

            try {
                GrabarCo.UpdateConfigMicrocredito(cf);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
            }
            JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
        } else {
            JOptionPane.showMessageDialog(null, "Algunos Datos son Necesarios");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_GrabarConfigActionPerformed

