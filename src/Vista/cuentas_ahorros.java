    private class GrillaCuentas extends Thread {

        public void run() {

            //Antes de Cargar el Jtable Ajustamos el ancho de las columnas con el Ancho que se nos antoje
            int cantidadRegistro = modelo.getRowCount();
            for (int i = 1; i <= cantidadRegistro; i++) {
                modelo.removeRow(0);
            }

            cuentas_ahorroDAO DAO = new cuentas_ahorroDAO();
            try {
                for (cuentas_ahorro em : DAO.todos()) {
                    String Datos[] = {String.valueOf(em.getCuenta()),
                        em.getSocio().getNombre(), 
                        em.getTipoahorro().getNombre(),
                        formatoFecha.format(em.getFechaapertura())};
                    modelo.addRow(Datos);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
            }

            tablacuentas.setRowSorter(new TableRowSorter(modelo));
            int cantFilas = tablacuentas.getRowCount();
            if (cantFilas > 0) {
                BotonEditar.setEnabled(true);
                BotonDelete.setEnabled(true);
                BotonPrint.setEnabled(true);
            } else {
                BotonEditar.setEnabled(false);
                BotonDelete.setEnabled(false);
                BotonPrint.setEnabled(false);
            }
        }
    }

