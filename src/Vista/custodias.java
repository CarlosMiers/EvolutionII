    private void imprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imprimirActionPerformed
        con = new Conexion();
        stm = con.conectar();
        try {
            Map parameters = new HashMap();

            Date FechaIni = ODate.de_java_a_sql(this.FechaInicial.getDate());
            Date FechaFin = ODate.de_java_a_sql(this.FechaFinal.getDate());

            parameters.put("cNombreEmpresa", Config.cNombreEmpresa);
            parameters.put("nCustodia", entidad.getText());
            parameters.put("FechaI", FechaIni);
            parameters.put("FechaF", FechaFin);

            JasperReport jr = null;
            URL url = getClass().getClassLoader().getResource("Reports/pagares_en_custodia.jasper");
            jr = (JasperReport) JRLoader.loadObject(url);
            JasperPrint masterPrint = null;
            //Se le incluye el parametro con el nombre parameters porque asi lo definimos
            masterPrint = JasperFillManager.fillReport(jr, parameters, stm.getConnection());
            JasperViewer ventana = new JasperViewer(masterPrint, false);
            ventana.setTitle("Vista Previa");
            ventana.setVisible(true);
           // this.impresion.setVisible(false);
        } catch (Exception e) {
            JDialog.setDefaultLookAndFeelDecorated(true);
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", 1);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_imprimirActionPerformed

