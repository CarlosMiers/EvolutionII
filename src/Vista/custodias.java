    private void BotonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGrabarActionPerformed
        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar los Datos? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {

            custodiaDAO grabar = new custodiaDAO();
            custodia cus = new custodia();
            int nestado = 0;
            if (estado.isSelected()) {
                nestado = 1;
            }
            cus.setNombre(nombre.getText());
            cus.setDireccion(direccion.getText());
            cus.setRuc(ruc.getText());
            cus.setContacto(contacto.getText());
            cus.setTelefono(telefono.getText());
            cus.setEstado(nestado);
            try {
                if (Integer.valueOf(codigo.getText()) == 0) {
                    grabar.insertarCustodia(cus);
                } else {
                    cus.setCodigo(Integer.valueOf(codigo.getText()));
                    grabar.actualizarCustodia(cus);
                }
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
            detalle_custodias.setVisible(false);
            detalle_custodias.setModal(false);

            GrillaCustodias GrillaOC = new GrillaCustodias();
            Thread HiloGrilla = new Thread(GrillaOC);
            HiloGrilla.start();
        }
    }//GEN-LAST:event_BotonGrabarActionPerformed

