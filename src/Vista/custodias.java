    private void BotonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarActionPerformed
        this.limpiar();
        if (Integer.valueOf(Config.cNivelUsuario) == 1) {
            int nFila = this.tablacustodia.getSelectedRow();
            if (nFila < 0) {
                JOptionPane.showMessageDialog(null, "Debe Seleccionar un Registro");
                this.tablacustodia.requestFocus();
                return;
            }
            this.codigo.setText(this.tablacustodia.getValueAt(nFila, 0).toString());
            custodiaDAO cusDAO = new custodiaDAO();
            custodia cus = null;
            try {
                cus = cusDAO.buscarId(Integer.valueOf(this.codigo.getText()));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
            }
            if (cus != null) {
                nombre.setText(cus.getNombre());
                ruc.setText(cus.getRuc());
                direccion.setText(cus.getDireccion());
                contacto.setText(cus.getContacto());
                telefono.setText(cus.getTelefono());
                if (cus.getEstado() == 0) {
                    this.estado.setSelected(false);
                } else {
                    this.estado.setSelected(true);
                }

                detalle_custodias.setModal(true);
                detalle_custodias.setSize(621, 390);
                //Establecemos un título para el jDialog
                detalle_custodias.setTitle("Modificar Datos de la Custodia");
                detalle_custodias.setLocationRelativeTo(null);
                detalle_custodias.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "USUARIO NO AUTORIZADO");
            }
        }

        //Envio como parametro el Codigo para saber si voy a modificar
        // TODO add your handling code here:
    }//GEN-LAST:event_BotonEditarActionPerformed

