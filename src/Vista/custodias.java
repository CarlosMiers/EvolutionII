    private class GrillaCustodias extends Thread {

        public void run() {

            //Antes de Cargar el Jtable Ajustamos el ancho de las columnas con el Ancho que se nos antoje
            int cantidadRegistro = modelo.getRowCount();
            for (int i = 1; i <= cantidadRegistro; i++) {
                modelo.removeRow(0);
            }

            custodiaDAO DAO = new custodiaDAO();
            try {
                for (custodia suc : DAO.todos()) {
                    String Datos[] = {String.valueOf(suc.getCodigo()), suc.getNombre(), suc.getDireccion(), suc.getTelefono(), suc.getContacto()};
                    modelo.addRow(Datos);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
            }

            tablacustodia.setRowSorter(new TableRowSorter(modelo));
            int cantFilas = tablacustodia.getRowCount();
            if (cantFilas > 0) {
                BotonEditar.setEnabled(true);
                BotonDelete.setEnabled(true);
                BotonPrint.setEnabled(true);
            } else {
                BotonEditar.setEnabled(false);
                BotonDelete.setEnabled(false);
                BotonPrint.setEnabled(false);
            }
        }
    }

