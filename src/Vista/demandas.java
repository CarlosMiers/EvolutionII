    private class GenerarReporte extends Thread {

        public void run() {
            con = new Conexion();
            stm = con.conectar();
            try {
                Map parameters = new HashMap();
                //Ahora que hay parametros le enviamos uno con el mismo nombre del que creamos
                //en el reporte
                parameters.put("cNombreEmpresa", Config.cNombreEmpresa);
                parameters.put("nCliente", idControl.getText().trim());
                JasperReport jr = null;
                URL url = getClass().getClassLoader().getResource("Reports/extracto_cuenta_clientes.jasper");
                jr = (JasperReport) JRLoader.loadObject(url);
                JasperPrint masterPrint = null;
                //Se le incluye el parametro con el nombre parameters porque asi lo definimos
                masterPrint = JasperFillManager.fillReport(jr, parameters, stm.getConnection());
                JasperViewer ventana = new JasperViewer(masterPrint, false);
                ventana.setTitle("Vista Previa");
                ventana.setVisible(true);
            } catch (Exception e) {
                JDialog.setDefaultLookAndFeelDecorated(true);
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", 1);
            }
        }
    }

    private class GrillaDemandas extends Thread {

        public void run() {
            con = new Conexion();
            stm = con.conectar();
            ResultSet results = null;
            dFechaInicio = ODate.de_java_a_sql(FechaInicial.getDate());
            dFechaFinal = ODate.de_java_a_sql(FechaFinal.getDate());
            cSql = "SELECT numero,fechainicio,cliente,clientes.nombre AS nombrecliente,demandas.abogado,abogados.nombre AS nombreabogado,";
            cSql += "motivodemanda.nombre AS nombremotivo,nombrecausa,juez";
            cSql += " FROM demandas ";
            cSql += " INNER JOIN clientes ";
            cSql += " ON clientes.codigo=demandas.cliente ";
            cSql += " INNER JOIN abogados ";
            cSql += " ON abogados.codigo=demandas.abogado ";
            cSql += " INNER JOIN motivodemanda ";
            cSql += " ON motivodemanda.codigo=demandas.motivo ";
            cSql = cSql + " WHERE demandas.fechainicio BETWEEN '" + dFechaInicio + "' AND '" + dFechaFinal + "'";
            cSql = cSql + " ORDER BY demandas.fechainicio";

            //Antes de Cargar el Jtable Ajustamos el ancho de las columnas con el Ancho que se nos antoje
            DefaultTableCellRenderer TablaRenderer = new DefaultTableCellRenderer();
            TablaRenderer.setHorizontalAlignment(SwingConstants.RIGHT); // aqui defines donde alinear 
            jTable1.getColumnModel().getColumn(0).setCellRenderer(TablaRenderer);

            int cantidadRegistro = modelo.getRowCount();
            for (int i = 1; i <= cantidadRegistro; i++) {
                modelo.removeRow(0);
            }
            try {
                results = stm.executeQuery(cSql);
                while (results.next()) {
                    // Se crea un array que será una de las filas de la tabla.
                    // Se rellena cada posición del array con una de las columnas de la tabla en base de datos.
                    Object[] fila = new Object[8]; // Hay 14 columnas en la tabla
                    fila[0] = results.getString("numero");
                    fila[1] = formatoFecha.format(results.getDate("fechainicio"));
                    fila[2] = results.getString("cliente");
                    fila[3] = results.getString("nombrecliente");
                    fila[4] = results.getString("nombreabogado");
                    fila[5] = results.getString("nombremotivo");
                    fila[6] = results.getString("nombrecausa");
                    fila[7] = results.getString("juez");
                    modelo.addRow(fila);          // Se añade al modelo la fila completa.
                }
                jTable1.setRowSorter(new TableRowSorter(modelo));
                int cantFilas = jTable1.getRowCount();
                if (cantFilas > 0) {
                    jButton1.setEnabled(true);
                    jButton3.setEnabled(true);
                    jButton4.setEnabled(true);
                } else {
                    jButton1.setEnabled(false);
                    jButton3.setEnabled(false);
                    jButton4.setEnabled(true);
                }
                results.close();
                stm.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(new JFrame(),
                        "El Sistema No Puede Ingresar a los Datos",
                        "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                System.out.println(ex);
            }
        }
    }
