    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ControlGrabado.REGISTRO_GRABADO = "SI";
        BDConexion BD = new BDConexion();
        String Operacion = this.jTextCodigo.getText();

        if (Operacion.isEmpty()) {
            BD.insertarRegistro("bancos", "nombre,nrocuenta,contacto,direccion,telefono,mail,tipo,estado", "'" + this.jTextnombre.getText() + "','" + this.jTextCuenta.getText() + "','" + this.jTextContacto.getText() + "','" + this.jTextDireccion.getText() + "','" + this.jTextFono.getText() + "','" + this.jTextMail.getText() + "','" + this.jComboCuenta.getSelectedIndex() + "','" + this.jComboEstado.getSelectedIndex() + "'");
        } else {
            BD.actualizarRegistro("bancos", " nombre='" + this.jTextnombre.getText() + "',direccion='" + this.jTextDireccion.getText() + "',telefono='" + this.jTextFono.getText() + "',contacto='" + this.jTextContacto.getText() + "',nrocuenta='" + this.jTextCuenta.getText() + "',mail='" + this.jTextMail.getText() + "',tipo='" + this.jComboCuenta.getSelectedIndex() + "',estado='" + this.jComboEstado.getSelectedIndex() + "'", "codigo= " + this.jTextCodigo.getText());
        }
        this.dispose();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

