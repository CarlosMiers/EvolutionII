    public void consultarTabla(String campoclave) {
        con = new Conexion();
        stm = con.conectar();

        //Inicializo Variables
        int nCuenta = 0;
        int nLocalidad = 0;
        String numero;
        int num;
        String cCodCuenta;
        String cCodLocalidad;

        ResultSet results = null;
        try {
            results = stm.executeQuery("select * from proveedores where codigo=" + this.codigo.getText());
            if (results.next()) {
                nCuenta = results.getInt("idcta");
                nLocalidad = results.getInt("localidad");
                this.nombre.setText(results.getString("nombre"));
                this.ruc.setText(results.getString("ruc"));
                this.nrotimbrado.setText(results.getString("timbrado"));
                this.vencetimbrado.setDate(results.getDate("vencimiento"));
                this.direccion.setText(results.getString("direccion"));
                this.telefono.setText(results.getString("telefono"));
                this.celular.setText(results.getString("fax"));
                this.mail.setText(results.getString("email"));
                this.contacto.setText(results.getString("contacto"));
                this.estado.setSelectedIndex(results.getInt("estado"));
                //Con esta funci√≥n mostramos los valores numericos con los puntos correspondientes
                stm.close();
                cCodLocalidad = selectCombo(localidad, "select codigo,nombre from localidades where codigo='" + nLocalidad + "'");
            }
        } catch (SQLException ex2) {
            JOptionPane.showMessageDialog(new JFrame(),
                    "El Sistema No Puede Ingresar a los Datos",
                    "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex2);
        }
    }

    public String selectCombo(JComboBox combo, String sql) {
        ResultSet codi = null;
        ComboBoxModel modelo = combo.getModel();
        int longitud = combo.getItemCount();
        int cod = 0;
        int c = 0;
        String codigo = "";
        Object ob = null;
        codi = BD.Select(sql);
        try {
            codi.first();
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        while (c < longitud) {
            combo.setSelectedIndex(c);
            ob = combo.getSelectedItem();
            cod = Integer.valueOf(((String[]) ob)[0]).intValue();
            try {
                if (cod == codi.getInt(1)) {
                    codigo = Integer.valueOf(cod).toString();
                    c = longitud;
                }
            } catch (SQLException ex) {
                System.out.println(ex);
            }
            c++;
        }
        return codigo;
    }

