    public void consultarTabla(String campoclave) {
        con = new Conexion();
        stm = con.conectar();
        ResultSet results = null;
        try {
            results = stm.executeQuery("select * from sucursales where codigo =" + this.jTextCodigo.getText());
            if (results.next()) {
                this.jTextnombre.setText(results.getString("nombre"));
                this.jTextDireccion.setText(results.getString("direccion"));
                this.jTextFono.setText(results.getString("telefono"));
                this.jTextResponsable.setText(results.getString("responsable"));
                this.jTextFonoResponsable.setText(results.getString("fonoresponsable"));
                this.jTextNombreConductor.setText(results.getString("nombreconductor"));
                this.jTextRucConductor.setText(results.getString("cedulaconductor"));
                this.jTextMarcaVehiculo.setText(results.getString("marcavehiculo"));
                this.jTextNroChapa.setText(results.getString("chapa"));
                this.nrofactura.setText(results.getString("factura"));
                this.notacredito.setText(results.getString("notacredito"));
                this.nrotimbrado.setText(results.getString("nrotimbrado"));
                this.vencetimbrado.setDate(results.getDate("vencetimbrado"));
                this.expedicion.setText(results.getString("expedicion"));
                this.expedicion_nota.setText(results.getString("expedicion_nota"));
            }
            stm.close();
        } catch (SQLException ex2) {
            JOptionPane.showMessageDialog(new JFrame(),
                    "El Sistema No Puede Ingresar a los Datos",
                    "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex2) {
            JOptionPane.showMessageDialog(new JFrame(),
                    "El Sistema No Puede Conectarse a la Base de Datos",
                    "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
        }
    }

