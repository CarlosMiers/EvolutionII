    private class GenerarReporte extends Thread {

        public void run() {
            int nFila = 0;
            List Resultados = new ArrayList();
            Detalleventasxcliente tipo;
            String cCantidad = null;
            String cPrecio = null;
            String cTotal = null;
            Date FechaInicio = ODate.de_java_a_sql(dFechaInicial.getDate());
            Date FechaFinal = ODate.de_java_a_sql(dFechaFinal.getDate());

            for (nFila = 0; nFila < jTable1.getRowCount(); nFila++) {
                cCantidad = jTable1.getValueAt(nFila, 5).toString();
                cCantidad = cCantidad.replace(".", "").replace(",", ".");

                cPrecio = jTable1.getValueAt(nFila, 6).toString();
                cPrecio = cPrecio.replace(".", "").replace(",", ".");

                cTotal = jTable1.getValueAt(nFila, 7).toString();
                cTotal = cTotal.replace(".", "").replace(",", ".");

                tipo = new Detalleventasxcliente(jTable1.getValueAt(nFila, 0).toString(), jTable1.getValueAt(nFila, 1).toString(), jTable1.getValueAt(nFila, 3).toString(), jTable1.getValueAt(nFila, 4).toString(), Double.valueOf(cCantidad), Double.valueOf(cPrecio), Integer.valueOf(jTable1.getValueAt(nFila, 8).toString()), Double.valueOf(cTotal), jTable1.getValueAt(nFila, 2).toString(), Integer.valueOf(jTable1.getValueAt(nFila, 9).toString()), jTable1.getValueAt(nFila, 10).toString());
                Resultados.add(tipo);

                System.out.println(tipo.getNombreproducto());
                System.out.println(tipo.getCantidad());
                System.out.println(tipo.getPrecio());
                System.out.println(tipo.getTotal());
                System.out.println(tipo.getNombreproducto());

            }

            try {
                Map parameters = new HashMap();
                //Ahora que hay parametros le enviamos uno con el mismo nombre del que creamos

                parameters.put("cNombreEmpresa", Config.cNombreEmpresa.trim());
                parameters.put("FechaInicio", FechaInicio);
                parameters.put("FechaFinal", FechaFinal);
                parameters.put("cMoneda", nombremoneda.getText());

                JasperReport jr = null;

                URL url = getClass().getClassLoader().getResource("Reports/detalle_venta_productosxcliente.jasper");
                jr = (JasperReport) JRLoader.loadObject(url);
                JasperPrint masterPrint = null;
                //Se le incluye el parametro con el nombre parameters porque asi lo definimos
                masterPrint = JasperFillManager.fillReport(jr, parameters, new JRBeanCollectionDataSource(Resultados));
                JasperViewer ventana = new JasperViewer(masterPrint, false);
                ventana.setTitle("Vista Previa");
                ventana.setVisible(true);
            } catch (Exception e) {
                JDialog.setDefaultLookAndFeelDecorated(true);
                JOptionPane.showMessageDialog(null, e, "Error", 1);
                System.out.println(e);
            }        // TODO add your handling code here:
        }
    }
