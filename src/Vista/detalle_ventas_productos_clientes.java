    private class GenerarConsultaVenta extends Thread {

        public void run() {
            int nCliente = Integer.valueOf(cliente.getText());
            int nMoneda = Integer.valueOf(moneda.getText());
            Date FechaI = ODate.de_java_a_sql(dFechaInicial.getDate());
            Date FechaF = ODate.de_java_a_sql(dFechaFinal.getDate());
            //Antes de Cargar el Jtable Ajustamos el ancho de las columnas con el Ancho que se nos antoje
            int cantidadRegistro = modelo.getRowCount();
            for (int i = 1; i <= cantidadRegistro; i++) {
                modelo.removeRow(0);
            }

            ventaDAO DAO = new ventaDAO();
            try {
                for (venta orden : DAO.DetalleVtaProductoxCliente(FechaI, FechaF, nCliente, producto.getText(), nMoneda)) {
                    String Datos[] = {formatosinpunto.format(orden.getFactura()), formatoFecha.format(orden.getFecha()), orden.getCliente().getNombre(), orden.getCodprodventa(), orden.getNombreproductoventa(), formato.format(orden.getCantidadventa()), formato.format(orden.getPrecioventa()), formato.format(orden.getTotalneto()), String.valueOf(orden.getPorcentaje()), String.valueOf(orden.getCliente().getCodigo()), orden.getSucursal().getNombre(),};
                    modelo.addRow(Datos);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
            }

            jTable1.setRowSorter(new TableRowSorter(modelo));
            int cantFilas = jTable1.getRowCount();
            if (cantFilas > 0) {
                listar.setEnabled(true);
            } else {
                listar.setEnabled(false);
            }
        }
    }

