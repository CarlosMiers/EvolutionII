    private void BotonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGrabarActionPerformed
        if (this.sede.getText().isEmpty() || this.sede.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Seleccione la Sede");
            this.sede.requestFocus();
            return;
        }
        if (this.nombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese del Nombre");
            this.nombre.requestFocus();
            return;
        }

        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar los Datos? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {

            edificio_sedeDAO grabar = new edificio_sedeDAO();
            edificio_sede edif = new edificio_sede();
            sucursalDAO sucDAO = new sucursalDAO();
            sucursal suc = null;

            try {
                suc = sucDAO.buscarId(Integer.valueOf(sede.getText()));
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }

            int nestado = 0;
            if (this.estado.isSelected()) {
                nestado = 1;
            }
            edif.setNombre(nombre.getText());
            edif.setIdsede(suc);
            edif.setDireccion(direccion.getText());
            edif.setTelefono(telefono.getText());
            edif.setNroaulas(Integer.valueOf(nroaulas.getText()));
            edif.setNropisos(Integer.valueOf(nropisos.getText()));
            edif.setEstado(nestado);
            try {
                if (Integer.valueOf(codigo.getText()) == 0) {
                    grabar.insertarEdificio(edif);
                } else {
                    edif.setCodigo(Integer.valueOf(codigo.getText()));
                    grabar.actualizarEdificio(edif);
                }
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
            detalle_edificio.setVisible(false);
            detalle_edificio.setModal(false);

            GrillaEdificioSede GrillaOC = new GrillaEdificioSede();
            Thread HiloGrilla = new Thread(GrillaOC);
            HiloGrilla.start();
        }
    }//GEN-LAST:event_BotonGrabarActionPerformed

