    private void BotonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarActionPerformed
        this.limpiar();
        if (Integer.valueOf(Config.cNivelUsuario) == 1) {
            int nFila = this.tablaedificios.getSelectedRow();
            if (nFila < 0) {
                JOptionPane.showMessageDialog(null, "Debe Seleccionar un Registro");
                this.tablaedificios.requestFocus();
                return;
            }

            this.codigo.setText(this.tablaedificios.getValueAt(nFila, 0).toString());
            edificio_sedeDAO sucDAO = new edificio_sedeDAO();
            edificio_sede edif = null;
            try {
                edif = sucDAO.buscarId(Integer.valueOf(this.codigo.getText()));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
            }
            if (edif != null) {
                nombre.setText(edif.getNombre());
                sede.setText(String.valueOf(edif.getIdsede().getCodigo()));
                nombresede.setText(edif.getIdsede().getNombre());
                direccion.setText(edif.getDireccion());
                telefono.setText(edif.getTelefono());
                nropisos.setText(String.valueOf(edif.getNropisos()));
                nroaulas.setText(String.valueOf(edif.getNroaulas()));
                
                if (edif.getEstado() == 0) {
                    this.estado.setSelected(false);
                } else {
                    this.estado.setSelected(true);
                }

                detalle_edificio.setModal(true);
                detalle_edificio.setSize(625, 410);
                //Establecemos un título para el jDialog
                detalle_edificio.setTitle("Modificar Datos del Edificio");
                detalle_edificio.setLocationRelativeTo(null);
                detalle_edificio.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "USUARIO NO AUTORIZADO");
            }
        }

        //Envio como parametro el Codigo para saber si voy a modificar
        // TODO add your handling code here:
    }//GEN-LAST:event_BotonEditarActionPerformed

