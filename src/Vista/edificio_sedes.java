    private void BotonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonDeleteActionPerformed
        if (Integer.valueOf(Config.cNivelUsuario) == 1) {
            int nFila = tablaedificios.getSelectedRow();
            String num = tablaedificios.getValueAt(nFila, 0).toString();
            Object[] opciones = {"   Si   ", "   No   "};
            int ret = JOptionPane.showOptionDialog(null, "Desea Eliminar el Registro ? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
            if (ret == 0) {
                edificio_sedeDAO sucDAO = new edificio_sedeDAO();
                try {
                    edificio_sede suc = sucDAO.buscarId((Integer.valueOf(num)));
                    if (suc == null) {
                        JOptionPane.showMessageDialog(null, "Registro no Existe");
                    } else {
                        sucDAO.eliminarEdificio(Integer.valueOf(num));
                        JOptionPane.showMessageDialog(null, "Registro Eliminado Exitosamente");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuario no Autorizado");
            }
            GrillaEdificioSede GrillaOC = new GrillaEdificioSede();
            Thread HiloGrilla = new Thread(GrillaOC);
            HiloGrilla.start();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_BotonDeleteActionPerformed

