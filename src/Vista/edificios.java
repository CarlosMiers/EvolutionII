    private void BuscarTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarTipoActionPerformed
        tipo_inmuebleDAO tpDAO = new tipo_inmuebleDAO();
        tipo_inmueble tp = null;
        try {
            tp = tpDAO.buscarId(Integer.valueOf(this.tipinmueble.getText()));
            if (tp.getCodigo() == 0) {
                GrillaTipo grillapa = new GrillaTipo();
                Thread hilopa = new Thread(grillapa);
                hilopa.start();
                BTipoInmueble.setModal(true);
                BTipoInmueble.setSize(482, 575);
                BTipoInmueble.setLocationRelativeTo(null);
                BTipoInmueble.setVisible(true);
                BTipoInmueble.setTitle("Buscar Tipo de Inmueble");
                moneda.requestFocus();
                BTipoInmueble.setModal(false);
            } else {
                nombretipo.setText(tp.getNombre());
                //Establecemos un título para el jDialog
                moneda.requestFocus();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
        }
    }//GEN-LAST:event_BuscarTipoActionPerformed

