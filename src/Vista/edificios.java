    private void ListarActionPerformed(ActionEvent evt) {//GEN-FIRST:event_ListarActionPerformed

        con = new Conexion();
        stm = con.conectar();
        int nFila = jTable1.getSelectedRow();

        try {
            Map parameters = new HashMap();

//Ahora que hay parametros le enviamos uno con el mismo nombre del que creamos
            //en el reporte
            String codinmueble= jTable1.getValueAt(nFila, 0).toString();
            parameters.put("cNombreEmpresa", Config.cNombreEmpresa);
            parameters.put("codinmueble",codinmueble);
            JasperReport jr = null;
            //URL url = getClass().getClassLoader().getResource("Reports/factura.jasper");
            URL url = getClass().getClassLoader().getResource("Reports/ficha_edificios.jasper");
            jr = (JasperReport) JRLoader.loadObject(url);
            JasperPrint masterPrint = null;
            //Se le incluye el parametro con el nombre parameters porque asi lo definimos
            masterPrint = JasperFillManager.fillReport(jr, parameters, stm.getConnection());
            JasperViewer ventana = new JasperViewer(masterPrint, false);
            ventana.setTitle("Vista Previa");
            ventana.setVisible(true);
            stm.close();
        } catch (Exception e) {
              System.out.println("ERROR "+e.getLocalizedMessage());
              System.out.println("ERROR 1 "+e.getMessage());
              System.out.println("ERROR 2 "+e.getCause());
            
        }

    }//GEN-LAST:event_ListarActionPerformed

