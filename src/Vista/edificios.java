    private class GrillaUnidades extends Thread {

        public void run() {
            int cantidadRegistro = modelo.getRowCount();
            for (int i = 1; i <= cantidadRegistro; i++) {
                modelo.removeRow(0);
            }

            edificioDAO edDAO = new edificioDAO();
            try {
                for (edificio edf : edDAO.Todos()) {
                    String Datos[] = {String.valueOf(edf.getIdunidad()), edf.getInmueble().getNomedif(), edf.getPropietario().getCedula(), edf.getNombrepropie(), edf.getCliente().getRuc(), edf.getCliente().getNombre(), edf.getCtactral(), edf.getUbicacion(), edf.getMedande(), edf.getNir(), formatea.format(edf.getAlquiler()), formatea.format(edf.getDepgtia())};
                    modelo.addRow(Datos);
                }
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }

            jTable1.setRowSorter(new TableRowSorter(modelo));
            int cantFilas = jTable1.getRowCount();
            if (cantFilas > 0) {
                Modificar.setEnabled(true);
                Eliminar.setEnabled(true);
                Listar.setEnabled(true);
            } else {
                Modificar.setEnabled(false);
                Eliminar.setEnabled(false);
                Listar.setEnabled(false);
            }
        }
    }

