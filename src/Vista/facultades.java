    private void BotonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarActionPerformed
        this.limpiar();
        if (Integer.valueOf(Config.cNivelUsuario) == 1) {
            int nFila = this.tablafacultad.getSelectedRow();
            if (nFila < 0) {
                JOptionPane.showMessageDialog(null, "Debe Seleccionar un Registro");
                this.tablafacultad.requestFocus();
                return;
            }

            this.codigo.setText(this.tablafacultad.getValueAt(nFila, 0).toString());
            facultadDAO sucDAO = new facultadDAO();
            facultad suc = null;
            try {
                suc = sucDAO.buscarId(Integer.valueOf(this.codigo.getText()));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
            }
            if (suc != null) {
                nombre.setText(suc.getNombre());
                rector.setText(suc.getRector());
                decano.setText(suc.getDecano());
                secretario_general.setText(suc.getSecretario_general());
                detalle_facultad.setModal(true);
                detalle_facultad.setSize(625, 318);
                //Establecemos un título para el jDialog
                detalle_facultad.setTitle("Modificar Datos de la Facultad");
                detalle_facultad.setLocationRelativeTo(null);
                detalle_facultad.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "USUARIO NO AUTORIZADO");
            }
        }

        //Envio como parametro el Codigo para saber si voy a modificar
        // TODO add your handling code here:
    }//GEN-LAST:event_BotonEditarActionPerformed

