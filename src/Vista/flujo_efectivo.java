    private void generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarActionPerformed
        if (this.ejercicioJSpinner.getValue().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el ejercicio fiscal");
            this.ejercicioJSpinner.requestFocus();
        } else {
            GenerarFlujoEfectivoDAO gfe = new GenerarFlujoEfectivoDAO();
            HashMap datos = gfe.generar((int) this.ejercicioJSpinner.getValue());
            try {
                System.out.println(Integer.parseInt(this.ejercicioJSpinner.getValue().toString()));
                int anio = Integer.parseInt(this.ejercicioJSpinner.getValue().toString());
                Map parameters = new HashMap();
                parameters.put("EMPRESA", Config.cNombreEmpresa.trim());
                //parameters.put("EMPRESA", Config.cNombreEmpresa);
                parameters.put("ANIO", anio);
                parameters.put("REPRESENTANTE", datos.get("representante")); 
                parameters.put("CONTADOR", datos.get("contador")); 
                parameters.put("AUDITOR", "............"); //CORREGIR
                parameters.put("COBROS_CLIENTES", datos.get("cobros"));
                parameters.put("COBROS_CUENTAS", BigDecimal.ZERO); //CORREGIR
                parameters.put("COBROS_PROVISIONES", BigDecimal.ZERO); //CORREGIR
                parameters.put("PAGOS_PROVEEDORES", datos.get("pagos"));
                parameters.put("PRESTAMOS_CLIENTES", datos.get("prestamos_clientes"));
                parameters.put("PAGOS_INTERESES", BigDecimal.ZERO); //CORREGIR
                parameters.put("PAGOS_IMPUESTO", BigDecimal.ZERO); //CORREGIR
                parameters.put("PAGOS_ANTICIPADOS", BigDecimal.ZERO); //CORREGIR
                parameters.put("SALDO_INICIAL", BigDecimal.ZERO); //CORREGIR
                JasperReport jr = null;

                URL url = getClass().getClassLoader().getResource("Reports/flujo_efectivo.jasper");
                jr = (JasperReport) JRLoader.loadObject(url);
                JasperPrint masterPrint = null;
                //Se le incluye el parametro con el nombre parameters porque asi lo definimos
                masterPrint = JasperFillManager.fillReport(jr, parameters, new JREmptyDataSource());
                JasperViewer ventana = new JasperViewer(masterPrint, false);
                ventana.setTitle("Vista Previa");
                ventana.setVisible(true);
            } catch (JRException e) {
                JDialog.setDefaultLookAndFeelDecorated(true);
                JOptionPane.showMessageDialog(null, e, "Error", 1);
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_generarActionPerformed

