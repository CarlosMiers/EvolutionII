    private class GenerarFacturaClientes extends Thread {

        public void run() {
            con = new Conexion();
            stm = con.conectar();
            stmcab = con.conectar();
            stmdet = con.conectar();
            stmfac = con.conectar();
            ResultSet results = null;
            Connection conn = null;
            Date FechaInicio = ODate.de_java_a_sql(fechainicio.getDate());
            Date FechaFinal = ODate.de_java_a_sql(fechafinal.getDate());
            UUID id = new UUID();

            String cCliente, cReferencia = null;
            String cGravadas10, cIva, cImporteIva = null;
            double nFactura, nGravadas, nIva, nImporteIva = 0.00;
            String cCodGasto = null;
            String cmoneda = Config.cMonedaDefecto;
            String cCantidad = "1";
            cIva = "10";
            cCodGasto = Config.cInteresPrestamo;

            String cSql = "SELECT cliente,sum(minteres) as total ";
            cSql = cSql + "FROM cuenta_clientes ";
            cSql = cSql + "WHERE comprobante IN (8,9,10) AND moneda=1  ";
            cSql = cSql + "AND cuenta_clientes.vencimiento BETWEEN " + "'" + FechaInicio + "'" + " AND " + "'" + FechaFinal + "'";
            cSql = cSql + "AND cuenta_clientes.saldo>0 and cuenta_clientes.minteres>0 ";
            cSql = cSql + "GROUP BY cliente";
            System.out.println(cSql);

            try {
                results = stm.executeQuery(cSql);
                String cFactura = nrofactura.getText();
                while (results.next()) {
                    cReferencia = UUID.crearUUID();
                    cReferencia = cReferencia.substring(1, 25);
                    //Obteniendo Datos de los Combodatos
                    //SE CAPTURAN LOS DATOS NUMERICOS
                    //     String cImporte = this.importe.getText();
                    System.out.println(results.getString("cliente"));
                    nGravadas = results.getDouble("total");
                    cCliente = results.getString("cliente");

                    nImporteIva = Math.round(nGravadas / 11);
                    cGravadas10 = String.valueOf(nGravadas);
                    cImporteIva = String.valueOf(nImporteIva);

                    String cCotizacion = "1";

                    String cSqlCab = "INSERT INTO cabecera_ventas (creferencia,fecha,factura,vencimiento,cliente,sucursal,moneda,giraduria,";
                    cSqlCab += "comprobante,cotizacion,vendedor,caja,exentas,gravadas10,gravadas5,totalneto,observacion,idusuario)";
                    cSqlCab += "VALUES ('" + cReferencia + "','" + FechaFinal + "','" + cFactura + "','" + FechaFinal + "','" + cCliente + "','" + "1" + "','" + cmoneda + "','" + "1" + "','";
                    cSqlCab += "2" + "','" + cCotizacion + "','" + "1" + "','" + "1" + "','" + "0" + "','" + cGravadas10 + "','" + "0" + "','" + cGravadas10 + "','" + "FACTURA POR INTERESES VENCIDOS" + "','" + Config.CodUsuario + "')";

                    String cPrecio = cGravadas10;

                    String cSqlDetalle = "INSERT INTO detalle_ventas(dreferencia, codprod, cantidad, prcosto, precio, monto, impiva, porcentaje)";
                    cSqlDetalle += "VALUES ('" + cReferencia + "','" + cCodGasto + "','" + cCantidad + "','" + '0' + "','" + cPrecio + "','" + cGravadas10 + "','" + cImporteIva + "','" + cIva + "')";

                    String cSqlFactura = "UPDATE sucursales SET factura= '" + cFactura + "'";

                    stmcab.executeUpdate(cSqlCab);
                    stmdet.executeUpdate(cSqlDetalle);
                    stmfac.executeUpdate(cSqlFactura);
                    nFactura = Double.valueOf(cFactura) + 1;
                    cFactura = String.valueOf(nFactura);
                }
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
            try {
                stm.close();
                stmcab.close();
                stmdet.close();
                stmfac.close();
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
        }
    }
