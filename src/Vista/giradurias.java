    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        nestado = 0;
        nplanilla = 0;
        int nFila = this.jTable1.getSelectedRow();
        this.idControl.setText(this.jTable1.getValueAt(nFila, 0).toString());
        giraduriaDAO giraduria = new giraduriaDAO();
        giraduria g = null;
        try {
            g = giraduria.buscarId(Integer.valueOf(idControl.getText()));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
        }
        if (g != null) {
            codigo.setEnabled(false);
            codigo.setText(idControl.getText());
            nombre.setText(g.getNombre());
            ruc.setText(g.getRuc());
            direccion.setText(g.getDireccion());
            telefono.setText(g.getTelefono());
            fax.setText(g.getFax());
            responsable.setText(g.getResponsable());
            nestado = g.getEstado();
            nplanilla = g.getPlanilla();
            comision.setText(formato.format(g.getComision()));
            iva.setText(formato.format(g.getIva()));
            if (nestado == 1) {
                estado.setSelected(true);
            } else {
                estado.setSelected(false);
            }
            if (nplanilla == 1) {
                planilla.setSelected(true);
            } else {
                planilla.setSelected(false);
            }
            //Establecemos un título para el jDialog

        }
        nombre.requestFocus();
        detalle_giradurias.setModal(true);
        detalle_giradurias.setSize(773, 500);
        //Establecemos un título para el jDialog
        detalle_giradurias.setTitle("Actualización");
        detalle_giradurias.setLocationRelativeTo(null);
        detalle_giradurias.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

