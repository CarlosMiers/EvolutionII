    private void GrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed
        giraduriaDAO grabarG = new giraduriaDAO();
        giraduria g = new giraduria();
        g.setNombre(nombre.getText());
        g.setDireccion(direccion.getText());
        g.setTelefono(telefono.getText());
        g.setFax(fax.getText());
        g.setResponsable(responsable.getText());
        if (planilla.isSelected()) {
            nplanilla = 1;
        } else {
            nplanilla = 0;
        }
        g.setPlanilla(nplanilla);
        if (estado.isSelected()) {
            nestado = 1;
        } else {
            nestado = 0;
        }
        g.setEstado(nestado);
        g.setRuc(ruc.getText());
        String cComision = "0";
        cComision = comision.getText();
        cComision = cComision.replace(".", "");
        cComision = cComision.replace(",", ".");
        String cIva = iva.getText();
        cIva = cIva.replace(".", "");
        cIva = cIva.replace(",", ".");
        g.setComision(Double.valueOf(cComision));
        g.setIva(Double.valueOf(cIva));
        if (Integer.valueOf(idControl.getText()) != 0) {
            g.setCodigo(Integer.valueOf(codigo.getText()));
        } else {
            g.setCodigo(Integer.valueOf(0));
        }
        if (Integer.valueOf(idControl.getText()) == 0) {
            // Agregar nuevo Cliente
            if (nombre.getText().compareTo("") != 0
                    && ruc.getText().compareTo("") != 0) {
                try {
                    grabarG.insertarGiraduria(g);
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Algunos Datos son Necesarios");
            }
        } else {
            //Actualizar Datos de Clientes
            if (codigo.getText().compareTo("") != 0 && nombre.getText().compareTo("") != 0
                    && ruc.getText().compareTo("") != 0) {
                try {
                    grabarG.actualizarGiraduria(g);
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Algunos Datos son Necesarios");
            }
        }

        this.detalle_giradurias.setModal(false);
        this.detalle_giradurias.setVisible(false);
        GrillaGiraduria GrillaGiraduria = new GrillaGiraduria();
        Thread HiloGrilla = new Thread(GrillaGiraduria);
        HiloGrilla.start();
        // TODO add your handling code here:
    }//GEN-LAST:event_GrabarActionPerformed

