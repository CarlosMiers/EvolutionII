    private class generarconsulta extends Thread {

        public void run() {

            //Dando formato a los datos tipo Fecha
            Date dFechaInicio = ODate.de_java_a_sql(dFechaInicial.getDate());
            Date dFechaFinal = ODate.de_java_a_sql(FechaFinal.getDate());

            cSql = "SELECT 'A' AS origen,nroboleta,fechapago,cuenta_clientes.cliente,cuenta_clientes.cuota,cuenta_clientes.numerocuota,";
            cSql += "cuenta_clientes.amortiza,";
            cSql += "cuenta_clientes.minteres,pagoexpress.gastos,pagoexpress.mora,pagoexpress.monto,clientes.nombre,cuenta_clientes.comprobante,";
            cSql += "comprobantes.nombre AS nombreprestamo,cuenta_clientes.documento ";
            cSql += "FROM pagoexpress ";
            cSql += "INNER JOIN clientes ";
            cSql += "ON clientes.codigo=pagoexpress.cliente ";
            cSql += "INNER JOIN cuenta_clientes ";
            cSql += "ON cuenta_clientes.iddocumento=pagoexpress.iddocumento ";
            cSql += "INNER JOIN comprobantes ";
            cSql += "ON comprobantes.codigo=cuenta_clientes.comprobante ";
            cSql += "WHERE fechapago BETWEEN " + "'" + dFechaInicio + "'" + " AND " + "'" + dFechaFinal + "'";
            cSql += " UNION ";
            cSql += "SELECT 'B' AS origen,CONVERT(numero, CHAR(15)) AS nroboleta,cobranzas.fecha AS fechapago,cobranzas.cliente,detalle_cobranzas.cuota,detalle_cobranzas.numerocuota, ";
            cSql += "detalle_cobranzas.amortiza, ";
            cSql += "detalle_cobranzas.minteres,detalle_cobranzas.gastos_cobranzas AS gastos,detalle_cobranzas.mora,detalle_cobranzas.pago AS monto, ";
            cSql += "clientes.nombre,detalle_cobranzas.comprobante, ";
            cSql += "comprobantes.nombre AS nombreprestamo,cuenta_clientes.documento ";
            cSql += "FROM cobranzas ";
            cSql += "INNER JOIN clientes ";
            cSql += "ON clientes.codigo=cobranzas.cliente ";
            cSql += "INNER JOIN detalle_cobranzas ";
            cSql += "ON detalle_cobranzas.iddetalle=cobranzas.idpagos ";
            cSql += "INNER JOIN comprobantes ";
            cSql += "ON comprobantes.codigo=detalle_cobranzas.comprobante ";
            cSql += "INNER JOIN cuenta_clientes ";
            cSql += "ON cuenta_clientes.iddocumento=detalle_cobranzas.idfactura ";
            cSql += "WHERE cobranzas.fecha BETWEEN " + "'" + dFechaInicio + "'" + " AND " + "'" + dFechaFinal + "'";
            cSql += " AND cobranzas.moneda =" + moneda.getText();
            cSql += " ORDER BY origen,comprobante,fechapago ";
            System.out.println(cSql);
            //Uso la Clase SimpleDateFormat para darle formato al campo fecha
            SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");

            //Instanciamos esta clase para alinear las celdas numericas a la derecha
            DefaultTableCellRenderer TablaRenderer = new DefaultTableCellRenderer();
            TablaRenderer.setHorizontalAlignment(SwingConstants.RIGHT); // aqui defines donde alinear 

            //Llamo a la clase conexion para conectarme a la base de datos
            con = new Conexion();
            stm = con.conectar();
            ResultSet results = null;

            jTable1.getColumnModel().getColumn(4).setCellRenderer(TablaRenderer);
            jTable1.getColumnModel().getColumn(5).setCellRenderer(TablaRenderer);
            jTable1.getColumnModel().getColumn(6).setCellRenderer(TablaRenderer);
            jTable1.getColumnModel().getColumn(7).setCellRenderer(TablaRenderer);
            jTable1.getColumnModel().getColumn(8).setCellRenderer(TablaRenderer);
            jTable1.getColumnModel().getColumn(9).setCellRenderer(TablaRenderer);

            int cantidadRegistro = modelo.getRowCount();
            for (int i = 1; i <= cantidadRegistro; i++) {
                modelo.removeRow(0);
            }
            try {
                results = stm.executeQuery(cSql);
                while (results.next()) {
                    //Instanciamos la Clase DecimalFormat para darle formato numerico a las celdas.
                    DecimalFormat formatea = new DecimalFormat("###,###.##");
                    // Se crea un array que será una de las filas de la tabla.
                    // Se rellena cada posición del array con una de las columnas de la tabla en base de datos.
                    Object[] fila = new Object[12]; // Cantidad de columnas en la tabla
                    fila[0] = results.getString("nroboleta");
                    fila[1] = formatoFecha.format(results.getDate("fechapago"));
                    fila[2] = results.getString("cliente");
                    fila[3] = results.getString("nombre");
                    fila[4] = results.getString("cuota");
                    fila[5] = formatea.format(results.getDouble("amortiza"));
                    fila[6] = formatea.format(results.getDouble("minteres"));
                    fila[7] = formatea.format(results.getDouble("mora"));
                    fila[8] = formatea.format(results.getDouble("gastos"));
                    fila[9] = formatea.format(results.getDouble("monto"));
                    fila[10] = results.getString("nombreprestamo");
                    fila[11] = results.getString("documento");
                    modelo.addRow(fila);          // Se añade al modelo la fila completa.
                }
                jTable1.setRowSorter(new TableRowSorter(modelo));
                jTable1.updateUI();
                int cantFilas = jTable1.getRowCount();
                if (cantFilas > 0) {
                    listar.setEnabled(true);
                } else {
                    listar.setEnabled(false);
                }
                stm.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(new JFrame(),
                        "El Sistema No Puede Ingresar a los Datos",
                        "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                System.out.println(ex);
            }

        }
    }

