    private class GrillaInmuebles extends Thread {

        public void run() {
            int cantidadRegistro = modelo.getRowCount();
            for (int i = 1; i <= cantidadRegistro; i++) {
                modelo.removeRow(0);
            }

            inmuebleDAO DAO = new inmuebleDAO();
            try {
                for (inmueble inmu : DAO.todos()) {
                    String Datos[] = {String.valueOf(inmu.getIdinmueble()), inmu.getTipinmueble().getNombre(), inmu.getNomedif(), inmu.getDiredif(), inmu.getFinca(), inmu.getCtactral()};
                    modelo.addRow(Datos);
                }
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
            jTable1.setRowSorter(new TableRowSorter(modelo));

            int cantFilas = jTable1.getRowCount();
            if (cantFilas > 0) {
                Modificar.setEnabled(true);
                Eliminar.setEnabled(true);
                Listar.setEnabled(true);
            } else {
                Modificar.setEnabled(false);
                Eliminar.setEnabled(false);
                Listar.setEnabled(false);
            }
        }
    }

