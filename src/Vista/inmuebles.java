    private void GrabarActionPerformed(ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed
        //Se inicia Proceso de Grabado de Registro
        //Se instancian las clases necesarias asociadas al modelado de Orden de Credito
        /* if (this.nombretipo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese la Descripción");
            this.nombretipo.requestFocus();
            return;
        }*/
        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar esta Operación? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {

            inmuebleDAO grabar = new inmuebleDAO();
            inmueble in = new inmueble();

            tipo_inmuebleDAO tipoDAO = new tipo_inmuebleDAO();
            tipo_inmueble pa = null;

            barrioDAO baDAO = new barrioDAO();
            barrio bar = null;

            localidadDAO locDAO = new localidadDAO();
            localidad loc = null;

            propietarioDAO pDAO = new propietarioDAO();
            propietario p = null;

            try {
                pa = tipoDAO.buscarId(Integer.valueOf(this.tipinmueble.getText()));
                bar = baDAO.buscarId(Integer.valueOf(this.coddis.getText()));
                loc = locDAO.buscarLocalidad(Integer.valueOf(this.localidad.getText()));
                p = pDAO.buscarId(Integer.valueOf(this.codpro.getText()));
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
            //CAPTURAMOS LOS DATOS DE LA CABECERA

            String cIvaExpensa=this.ivaexpensa.getText();
            cIvaExpensa = cIvaExpensa.replace(".", ",");
            in.setIvaexpensa(Double.valueOf(cIvaExpensa));
            in.setNomedif(this.nomedif.getText());
            in.setDiredif(this.diredif.getText());
            in.setFinca(this.finca.getText());
            in.setCtactral(this.ctactral.getText());
            in.setCodpro(p);
            in.setTipinmueble(pa);
            in.setCoddis(bar);
            in.setCodloca(loc);
            in.setObservacion(this.observacion.getText());
            in.setReglainter(this.reglamento.getText());

            //CAPTURAR PROPIETARIO
            int totalRow = modelopropietario.getRowCount();
            totalRow -= 1;

            String propietario = "[";
            for (int i = 0; i <= (totalRow); i++) {
                //Capturo y valido Producto
                String cPropietario = modelopropietario.getValueAt(i, 0).toString();
                //Capturo cantidad    
                String linea = "{idpropietario : " + cPropietario + "},";
                propietario += linea;
            }
            if (!propietario.equals("[")) {
                propietario = propietario.substring(0, propietario.length() - 1);
            }
            propietario += "]";

            if (Integer.valueOf(this.idinmueble.getText()) == 0) {
                try {
                    grabar.insertarInmuebles(in, propietario);
                    JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
            } else {
                //Actualizar Orden de Credito
                try {
                    in.setIdinmueble(Integer.valueOf(this.idinmueble.getText()));
                    inmueble_propietarioDAO delDAO = new inmueble_propietarioDAO();
                    delDAO.borrarpropietario(in.getIdinmueble());
                    grabar.ActualizarInmueble(in, propietario);
                    JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
            }
            detalle_inmuebles.setVisible(false);
            this.detalle_inmuebles.setModal(false);
            GrillaInmuebles GrillaP = new GrillaInmuebles();
            Thread HiloGrilla = new Thread(GrillaP);
            HiloGrilla.start();
        }
    }//GEN-LAST:event_GrabarActionPerformed

