    private void plazoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_plazoKeyPressed
        if (evt.getKeyCode() == 10 || evt.getKeyCode() == KeyEvent.VK_DOWN) {
            String p = precio.getText().replace(""," ");
            String c = cuotas.getText().replace(""," ");
            if(p.equals("") || p.equals("0") ){
                JOptionPane.showConfirmDialog(null, "Por Favor Ingrese Precio", "ATENCION", JOptionPane.CLOSED_OPTION);
                precio.requestFocus();
            }else if (c.equals("") || c.equals("0")){
                JOptionPane.showConfirmDialog(null, "Por Favor Ingrese Cuotas", "ATENCION", JOptionPane.CLOSED_OPTION);
                cuotas.requestFocus();
            }else{
                this.Grabar.doClick();
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_UP) {
            this.cuotas.requestFocus();
        } 
    }//GEN-LAST:event_plazoKeyPressed

