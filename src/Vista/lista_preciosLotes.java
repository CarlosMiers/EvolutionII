    private void GrabarActionPerformed(ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed

        if (this.lote.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese Código de Lote");
            this.lote.requestFocus();
            return;
        }

        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar esta Operación? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {
            //Date Fecha = ODate.de_java_a_sql(fecha.getDate());
            lista_precios_lotesDAO grabar = new lista_precios_lotesDAO();
            lista_precios_lotes preciolote = new lista_precios_lotes();

            lotesDAO loteDAO = new lotesDAO();
            lote lot = null;

            try {
                lot = loteDAO.buscarId(Integer.valueOf(this.lote.getText()));
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }

            //CAPTURAMOS LOS DATOS DE LA CABECERA
            preciolote.setIdlista(Integer.valueOf(this.numero.getText()));
            lot.setCodigo(Integer.valueOf(lote.getText()));
            preciolote.setLote(lot);
            preciolote.setPlazo(Integer.valueOf(plazo.getValue().toString()));
            String cCuota = cuotas.getText();
            String cImporte = precio.getText();
            cImporte = cImporte.replace(".", "").replace(",", ".");
            cCuota = cCuota.replace(".", "").replace(",", ".");
            BigDecimal nCuota = new BigDecimal(cCuota);
            BigDecimal nImporte = new BigDecimal(cImporte);
            preciolote.setCuotas(nCuota);
            preciolote.setPrecio(nImporte);
            preciolote.setDescripcion(nombreprecio.getText());

            //EMPEZAMOS A CAPTURAR DATOS NUMERICOS
            if (Integer.valueOf(this.numero.getText()) == 0) {
                try {
                    grabar.insertarlista(preciolote);
                    JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
            } else {
                //Actualizar 
                try {
                    preciolote.setIdlista(Integer.valueOf(numero.getText()));
                    grabar.actualizarlista(preciolote);
                    JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
            }
            detalle_precio_lote.setVisible(false);
            this.detalle_precio_lote.setModal(false);
            GrillaListaPreciosLotes GrillaLlega = new GrillaListaPreciosLotes();
            Thread HiloGrilla = new Thread(GrillaLlega);
            HiloGrilla.start();
        }

    }//GEN-LAST:event_GrabarActionPerformed

