    private void ModificarActionPerformed(ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        idControl.setText("2");
        // int nunidad = 0;
        this.limpiar();
        if (Integer.valueOf(Config.cNivelUsuario) < 3) {
            int nFila = this.jTable1.getSelectedRow();
            if (nFila < 0) {
                JOptionPane.showMessageDialog(null, "Debe Seleccionar un Registro");
                this.jTable1.requestFocus();
                return;
            }

            this.numero.setText(this.jTable1.getValueAt(nFila, 0).toString());
            this.numero.setEnabled(false);
            lista_precios_lotesDAO llegaDAO = new lista_precios_lotesDAO();
            lista_precios_lotes preciolista = null;
            try {
                preciolista = llegaDAO.buscarId(Integer.valueOf(this.numero.getText()));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
            }
            if (preciolista != null) {
                lote.setText(preciolista.getLote().getCodigo()+"");
                nombrelote.setText(preciolista.getLote().getNombre());
                plazo.setValue(preciolista.getPlazo());
                cuotas.setText(formatea.format(preciolista.getCuotas()));
                precio.setText(formatea.format(preciolista.getPrecio()));
                nombreprecio.setText(preciolista.getDescripcion());
                detalle_precio_lote.setModal(true);
                //                        (Ancho,Alto)
                detalle_precio_lote.setSize(390, 320);
                //Establecemos un título para el jDialog
                detalle_precio_lote.setTitle("Modificar Precio de Lote");
                detalle_precio_lote.setLocationRelativeTo(null);
                detalle_precio_lote.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "USUARIO NO AUTORIZADO");
            }
        }
    }//GEN-LAST:event_ModificarActionPerformed

    private void EliminarActionPerformed(ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        int nFila = jTable1.getSelectedRow();
        String num = jTable1.getValueAt(nFila, 0).toString();
        if (Integer.valueOf(Config.cNivelUsuario) == 1) {
            Object[] opciones = {"   Si   ", "   No   "};
            int ret = JOptionPane.showOptionDialog(null, "Desea Eliminar el Registro ? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
            if (ret == 0) {
                lista_precios_lotesDAO preciosDAO = new lista_precios_lotesDAO();
                try {
                    lista_precios_lotes lista = preciosDAO.buscarId(Integer.valueOf(num));
                    if (lista == null) {
                        JOptionPane.showMessageDialog(null, "Registro no Existe");
                    } else {
                        preciosDAO.eliminarlista(Integer.valueOf(num));
                        JOptionPane.showMessageDialog(null, "Registro Eliminado Exitosamente");
                        GrillaListaPreciosLotes GrillaOC = new GrillaListaPreciosLotes();
                        Thread HiloGrilla = new Thread(GrillaOC);
                        HiloGrilla.start();
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "USUARIO NO AUTORIZADO");
        }
        
    }//GEN-LAST:event_EliminarActionPerformed

