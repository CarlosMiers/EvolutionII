    private void GrabarActionPerformed(ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed

        if (this.nombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el Nombre");
            this.nombre.requestFocus();
            return;
        }

        if (this.loteamiento.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el Propietario");
            this.loteamiento.requestFocus();
            return;
        }

        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar esta OperaciÃ³n? ", "ConfirmaciÃ³n", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {

            // Date FechaProceso = ODate.de_java_a_sql(fecha.getDate());
            manzanaDAO grabar = new manzanaDAO();
            manzana ma = new manzana();
            loteamientoDAO lotDAO = new loteamientoDAO();
            loteamiento lot = null;

            try {
                lot = lotDAO.buscarId(Integer.valueOf(this.loteamiento.getText()));
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }

            //CAPTURAMOS LOS DATOS DE LA CABECERA
            ma.setCodigo(Integer.valueOf(this.codigo.getText()));
            ma.setNombre(this.nombre.getText());
            ma.setSuperficie(Double.valueOf(this.superficie.getText()));
            ma.setLotes(Integer.valueOf(this.lotes.getText()));
            ma.setLoteamiento(lot);
            //EMPEZAMOS A CAPTURAR DATOS NUMERICOS

            if (Integer.valueOf(this.codigo.getText()) == 0) {
                try {
                    grabar.insertarmanzana(ma);
                    JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
            } else {
                //Actualizar 
                try {
                    ma.setCodigo(Integer.valueOf(this.codigo.getText()));
                    grabar.actualizarmanzana(ma);
                    JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
            }
            detalle_manzana.setVisible(false);
            this.detalle_manzana.setModal(false);
            GrillaManzana GrillaAp = new GrillaManzana();
            Thread HiloGrilla = new Thread(GrillaAp);
            HiloGrilla.start();
        }

    }//GEN-LAST:event_GrabarActionPerformed

