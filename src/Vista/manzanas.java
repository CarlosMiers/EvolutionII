    private void EliminarActionPerformed(ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        int nFila = jTable1.getSelectedRow();
        String num = jTable1.getValueAt(nFila, 0).toString();

        if (Config.cNivelUsuario.equals("1")) {
            Object[] opciones = {"   Si   ", "   No   "};
            int ret = JOptionPane.showOptionDialog(null, "Desea Eliminar el Registro ? ", "ConfirmaciÃ³n", 0, 3, null, opciones, opciones[0]);
            if (ret == 0) {
                manzanaDAO manzanaDAO = new manzanaDAO();
                try {
                    manzana manzana = manzanaDAO.buscarId(Integer.valueOf(num));
                    if (manzana == null) {
                        JOptionPane.showMessageDialog(null, "Registro no Existe");
                    } else {
                        manzanaDAO.eliminarmanzana(Integer.valueOf(num));
                        JOptionPane.showMessageDialog(null, "Registro Eliminado Exitosamente");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "USUARIO NO AUTORIZADO");
        }
        GrillaManzana GrillaPr = new GrillaManzana();
        Thread HiloGrilla = new Thread(GrillaPr);
        HiloGrilla.start();

    }//GEN-LAST:event_EliminarActionPerformed

