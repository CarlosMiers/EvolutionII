    private void ModificarActionPerformed(ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        idControl.setText("2");
        this.limpiar();
        if (Integer.valueOf(Config.cNivelUsuario) == 1) {
            int nFila = this.jTable1.getSelectedRow();
            if (nFila < 0) {
                JOptionPane.showMessageDialog(null, "Debe Seleccionar un Registro");
                this.jTable1.requestFocus();
                return;
            }
            this.codigo.setText(this.jTable1.getValueAt(nFila, 0).toString());
            this.codigo.setEnabled(false);
            manzanaDAO maDAO = new manzanaDAO();
            manzana ma = null;
            try {
                ma = maDAO.buscarId(Integer.valueOf(this.codigo.getText()));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
            }
            if (ma != null) {

                //codigo.setText(codigo.getText());
                nombre.setText(ma.getNombre());
                loteamiento.setText(String.valueOf(ma.getLoteamiento().getCodigo()));
                nombreloteamiento.setText(ma.getLoteamiento().getNombre());
                lotes.setText(String.valueOf(ma.getLotes()));
                superficie.setText(formatea.format(ma.getSuperficie()));
                detalle_manzana.setModal(true);
                //                        (Ancho,Alto)
                detalle_manzana.setSize(500, 310);
                //Establecemos un tÃ­tulo para el jDialog
                detalle_manzana.setTitle("Modificar Datos de la Manzana");
                detalle_manzana.setLocationRelativeTo(null);
                detalle_manzana.setVisible(true);

            } else {
                JOptionPane.showMessageDialog(null, "USUARIO NO AUTORIZADO");
            }
        }
    }//GEN-LAST:event_ModificarActionPerformed

