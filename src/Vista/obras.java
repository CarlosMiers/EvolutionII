    private void BotonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGrabarActionPerformed
        if (this.nombre.getText().isEmpty() || this.nombre.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Ingrese la Descripción de la Sucursal");
            this.nombre.requestFocus();
            return;
        }

        if (this.contacto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el nombre del contacto");
            this.contacto.requestFocus();
            return;
        }

        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar los Datos? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {

            obraDAO grabar = new obraDAO();
            obra ob = new obra();

            localidadDAO loDAO = new localidadDAO();
            localidad l = null;

            clienteDAO cliDAO = new clienteDAO();
            cliente cli = null;
            try {
                l = loDAO.buscarLocalidad(1);
                cli = cliDAO.buscarId(Integer.valueOf(cliente.getText()));
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }

            int nestado = 0;
            if (this.estado.isSelected()) {
                nestado = 1;
            }
            ob.setNombre(nombre.getText());
            ob.setDireccion(direccion.getText());
            ob.setTelefonocontacto(fono.getText());
            ob.setContacto(contacto.getText());
            ob.setFax(fax.getText());
            ob.setEstado(nestado);
            ob.setLocalidad(l);
            ob.setPropietario(cli);
            try {
                if (Integer.valueOf(codigo.getText()) == 0) {
                    grabar.insertarObra(ob);
                } else {
                    ob.setCodigo(Integer.valueOf(codigo.getText()));
                    grabar.actualizarObra(ob);
                }
            } catch (SQLException ex) {
                Exceptions.printStackTrace(ex);
            }
            detalle_obras.setVisible(false);
            detalle_obras.setModal(false);

            GrillaObras GrillaOC = new GrillaObras();
            Thread HiloGrilla = new Thread(GrillaOC);
            HiloGrilla.start();
        }
    }//GEN-LAST:event_BotonGrabarActionPerformed

