    private void clienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clienteActionPerformed
        if (cliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No puede dejar en Blanco");
            cliente.requestFocus();
            return;
        }

        if (cliente.getText().equals("0")) {
            cliente.setText("0");
            nombrecliente.setText("Todos los Clientes");
            nTipo.requestFocus();
        } else {
            clienteDAO cliDAO = new clienteDAO();
            cliente cli = null;
            try {
                cli = cliDAO.buscarId(Integer.valueOf(this.cliente.getText()));
                if (cli.getCodigo() == 0) {
                    this.buscarCliente.doClick();
                } else {
                    nombrecliente.setText(cli.getNombre());
                    //Establecemos un t铆tulo para el jDialog
                }
                nTipo.requestFocus();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_clienteActionPerformed

