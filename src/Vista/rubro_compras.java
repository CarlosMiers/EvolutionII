    private void GrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed
        rubro_compraDAO grabarR = new rubro_compraDAO();
        rubro_compra r = new rubro_compra();
        planDAO plDAO = new planDAO();
        plan pl = null;

        try {
            pl = plDAO.buscarId(this.idcta.getText());
        } catch (SQLException ex) {
            Exceptions.printStackTrace(ex);
        }

        r.setNombre(nombre.getText());
        r.setIdcta(pl);
        if (Integer.valueOf(idControl.getText()) == 0) {
            // Agregar nuevo Cliente
            if (nombre.getText().compareTo("") != 0 && idcta.getText().compareTo("") != 0) {
                try {
                    grabarR.insertarubrobanco(r);
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Algunos Datos son Necesarios");
            }
        } else {
            if (nombre.getText().compareTo("") != 0 && idcta.getText().compareTo("") != 0) {
                try {
                    r.setCodigo(Integer.valueOf(codigo.getText()));
                    grabarR.actualizarrubrobanco(r);
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Algunos Datos son Necesarios");
            }
        }

        this.detalle_rubro.setModal(false);
        this.detalle_rubro.setVisible(false);
        GrillaRubroBancos GrillaR = new GrillaRubroBancos();
        Thread HiloGrilla = new Thread(GrillaR);
        HiloGrilla.start();
        // TODO add your handling code here:
    }//GEN-LAST:event_GrabarActionPerformed

