    private void GrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed
        //Se inicia Proceso de Grabado de Registro
        //Se instancian las clases necesarias asociadas al modelado de Orden de Credito
        if (this.nombre.getText().isEmpty() || this.nombre.getText().equals("0")) {
            JOptionPane.showMessageDialog(null, "Ingrese la Descripción por Favor");
            this.nombre.requestFocus();
            return;
        }
        
        Object[] opciones = {"   Si   ", "   No   "};
        int ret = JOptionPane.showOptionDialog(null, "Desea Guardar esta Operación? ", "Confirmación", 0, 3, null, opciones, opciones[0]);
        if (ret == 0) {
        
        seccionDAO grabar = new seccionDAO();
        seccion secc = new seccion();

        if (Integer.valueOf(codigo.getText()) > 0) {
            secc.setCodigo(Integer.valueOf(codigo.getText()));
        }    //CAPTURAMOS LOS DATOS DE LA CABECERA
        secc.setNombre(this.nombre.getText());

        if (Integer.valueOf(this.codigo.getText()) == 0) {
            try {
                grabar.insertarseccion(secc);
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
                this.Limpiar();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
            }
        } else {
            try {
                grabar.actualizarseccion(secc);
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
                this.Limpiar();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
            }

        }

        GrillaSeccion GrillaSecc = new GrillaSeccion();
        Thread HiloGrilla = new Thread(GrillaSecc);

        HiloGrilla.start();
        }
    }//GEN-LAST:event_GrabarActionPerformed

