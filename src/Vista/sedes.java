    private void BotonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarActionPerformed
        this.limpiar();
        if (Integer.valueOf(Config.cNivelUsuario) == 1) {
            int nFila = this.tablasucursal.getSelectedRow();
            if (nFila < 0) {
                JOptionPane.showMessageDialog(null, "Debe Seleccionar un Registro");
                this.tablasucursal.requestFocus();
                return;
            }

            this.codigo.setText(this.tablasucursal.getValueAt(nFila, 0).toString());
            sucursalDAO sucDAO = new sucursalDAO();
            sucursal suc = null;
            try {
                suc = sucDAO.buscarId(Integer.valueOf(this.codigo.getText()));
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
            }
            if (suc != null) {
                nombre.setText(suc.getNombre());
                direccion.setText(suc.getDireccion());
                fono.setText(suc.getTelefono());
                responsable.setText(suc.getResponsable());
                fonogerente.setText(suc.getFonoresponsable());
                detalle_sucursal.setModal(true);
                detalle_sucursal.setSize(625, 335);
                //Establecemos un título para el jDialog
                detalle_sucursal.setTitle("Modificar Datos de la Sede");
                detalle_sucursal.setLocationRelativeTo(null);
                detalle_sucursal.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "USUARIO NO AUTORIZADO");
            }
        }

        //Envio como parametro el Codigo para saber si voy a modificar
        // TODO add your handling code here:
    }//GEN-LAST:event_BotonEditarActionPerformed

