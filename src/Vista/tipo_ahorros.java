    private void GrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed
        tipo_ahorroDAO grabarG = new tipo_ahorroDAO();
        tipo_ahorro g = new tipo_ahorro();
        g.setNombre(nombre.getText());
        g.setPagointeres(pagointeres.getSelectedItem().toString());
        String cTasa = interesanual.getText();
        cTasa = cTasa.replace(",",".");
        g.setInteresanual(Double.valueOf(cTasa));
        if (Integer.valueOf(codigo.getText()) == 0) {
            // Agregar nuevo Cliente
            if (nombre.getText().compareTo("") != 0 ) {
                try {
                    grabarG.insertarTipoAhorro(g);
                    JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
            }
        } else {
            g.setCodigo(Integer.valueOf(codigo.getText()));
            if (nombre.getText().compareTo("") != 0) {
                try {
                    grabarG.actualizarTipoAhorro(g);
                    JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
                }
            }
        }
        this.detalle_tipo.setModal(false);
        this.detalle_tipo.setVisible(false);
        GrillaTipoAhorro GrillaAhorro = new GrillaTipoAhorro();
        Thread HiloAhorro = new Thread(GrillaAhorro);
        HiloAhorro.start();
        // TODO add your handling code here:
    }//GEN-LAST:event_GrabarActionPerformed

